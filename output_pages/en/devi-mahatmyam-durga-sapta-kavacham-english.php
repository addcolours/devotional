                  <span style="font-family:NotoSans; line-height:150%;">
<p>ōṃ namaśchaṇḍikāyai</p>
<p><b>nyāsaḥ</b><br>
asya śrī chaṇḍī kavachasya । brahmā ṛṣiḥ । anuṣṭup Chandaḥ ।<br>
chāmuṇḍā dēvatā । aṅganyāsōkta mātarō bījam । navāvaraṇō mantraśaktiḥ । digbandha dēvatāḥ tatvam । śrī jagadambā prītyarthē  saptaśatī pāṭhāṅgatvēna  japē viniyōgaḥ ॥<br>
 <br>
ōṃ namaśchaṇḍikāyai</p>
<p>mārkaṇḍēya uvācha ।<br>
ōṃ yadguhyaṃ paramaṃ lōkē sarvarakṣākaraṃ nṛṇām ।<br>
yanna kasyachidākhyātaṃ tanmē brūhi pitāmaha ॥ 1 ॥</p>
<p>brahmōvācha ।<br>
asti guhyatamaṃ vipra sarvabhūtōpakārakam ।<br>
dēvyāstu kavachaṃ puṇyaṃ tachChṛṇuṣva mahāmunē ॥ 2 ॥</p>
<p>prathamaṃ śailaputrī cha dvitīyaṃ brahmachāriṇī ।<br>
tṛtīyaṃ chandraghaṇṭēti kūṣmāṇḍēti chaturthakam ॥ 3 ॥</p>
<p>pañchamaṃ skandamātēti ṣaṣṭhaṃ kātyāyanīti cha ।<br>
saptamaṃ kālarātrīti mahāgaurīti chāṣṭamam ॥ 4 ॥</p>
<p>navamaṃ siddhidātrī cha navadurgāḥ prakīrtitāḥ ।<br>
uktānyētāni nāmāni brahmaṇaiva mahātmanā ॥ 5 ॥</p>
<p>agninā dahyamānastu śatrumadhyē gatō raṇē ।<br>
viṣamē durgamē chaiva bhayārtāḥ śaraṇaṃ gatāḥ ॥ 6 ॥</p>
<p>na tēṣāṃ jāyatē kiñchidaśubhaṃ raṇasaṅkaṭē ।<br>
nāpadaṃ tasya paśyāmi śōkaduḥkhabhayaṃ na hi ॥ 7 ॥</p>
<p>yaistu bhaktyā smṛtā nūnaṃ tēṣāṃ vṛddhiḥ prajāyatē ।<br>
yē tvāṃ smaranti dēvēśi rakṣasē tānnasaṃśayaḥ ॥ 8 ॥</p>
<p>prētasaṃsthā tu chāmuṇḍā vārāhī mahiṣāsanā ।<br>
aindrī gajasamārūḍhā vaiṣṇavī garuḍāsanā ॥ 9 ॥</p>
<p>māhēśvarī vṛṣārūḍhā kaumārī śikhivāhanā ।<br>
lakṣmīḥ padmāsanā dēvī padmahastā haripriyā ॥ 10 ॥</p>
<p>śvētarūpadharā dēvī īśvarī vṛṣavāhanā ।<br>
brāhmī haṃsasamārūḍhā sarvābharaṇabhūṣitā ॥ 11 ॥</p>
<p>ityētā mātaraḥ sarvāḥ sarvayōgasamanvitāḥ ।<br>
nānābharaṇāśōbhāḍhyā nānāratnōpaśōbhitāḥ ॥ 12 ॥</p>
<p>dṛśyantē rathamārūḍhā dēvyaḥ krōdhasamākulāḥ ।<br>
śaṅkhaṃ chakraṃ gadāṃ śaktiṃ halaṃ cha musalāyudham ॥ 13 ॥</p>
<p>khēṭakaṃ tōmaraṃ chaiva paraśuṃ pāśamēva cha ।<br>
kuntāyudhaṃ triśūlaṃ cha śārṅgamāyudhamuttamam ॥ 14 ॥</p>
<p>daityānāṃ dēhanāśāya bhaktānāmabhayāya cha ।<br>
dhārayantyāyudhānītthaṃ dēvānāṃ cha hitāya vai ॥ 15 ॥</p>
<p>namastē'stu mahāraudrē mahāghōraparākramē ।<br>
mahābalē mahōtsāhē mahābhayavināśini ॥ 16 ॥</p>
<p>trāhi māṃ dēvi duṣprēkṣyē śatrūṇāṃ bhayavardhini ।<br>
prāchyāṃ rakṣatu māmaindrī āgnēyyāmagnidēvatā ॥ 17 ॥ </p>
<p>dakṣiṇē'vatu vārāhī nairṛtyāṃ khaḍgadhāriṇī ।<br>
pratīchyāṃ vāruṇī rakṣēdvāyavyāṃ mṛgavāhinī ॥ 18 ॥ </p>
<p>udīchyāṃ pātu kaumārī aiśānyāṃ śūladhāriṇī ।<br>
ūrdhvaṃ brahmāṇī mē rakṣēdadhastādvaiṣṇavī tathā ॥ 19 ॥</p>
<p>ēvaṃ daśa diśō rakṣēchchāmuṇḍā śavavāhanā ।<br>
jayā mē chāgrataḥ pātu vijayā pātu pṛṣṭhataḥ ॥ 20 ॥</p>
<p>ajitā vāmapārśvē tu dakṣiṇē chāparājitā ।<br>
śikhāmudyōtinī rakṣēdumā mūrdhni vyavasthitā ॥ 21 ॥</p>
<p>mālādharī lalāṭē cha bhruvau rakṣēdyaśasvinī ।<br>
trinētrā cha bhruvōrmadhyē yamaghaṇṭā cha nāsikē ॥ 22 ॥</p>
<p>śaṅkhinī chakṣuṣōrmadhyē śrōtrayōrdvāravāsinī ।<br>
kapōlau kālikā rakṣētkarṇamūlē tu śāṅkarī ॥ 23 ॥</p>
<p>nāsikāyāṃ sugandhā cha uttarōṣṭhē cha charchikā ।<br>
adharē chāmṛtakalā jihvāyāṃ cha sarasvatī ॥ 24 ॥</p>
<p>dantān rakṣatu kaumārī kaṇṭhadēśē tu chaṇḍikā ।<br>
ghaṇṭikāṃ chitraghaṇṭā cha mahāmāyā cha tālukē ॥ 25 ॥</p>
<p>kāmākṣī chibukaṃ rakṣēdvāchaṃ mē sarvamaṅgaḻā ।<br>
grīvāyāṃ bhadrakāḻī cha pṛṣṭhavaṃśē dhanurdharī ॥ 26 ॥</p>
<p>nīlagrīvā bahiḥ kaṇṭhē nalikāṃ nalakūbarī ।<br>
skandhayōḥ khaḍginī rakṣēdbāhū mē vajradhāriṇī ॥ 27 ॥</p>
<p>hastayōrdaṇḍinī rakṣēdambikā chāṅgulīṣu cha ।<br>
nakhāñChūlēśvarī rakṣētkukṣau rakṣētkulēśvarī ॥ 28 ॥</p>
<p>stanau rakṣēnmahādēvī manaḥśōkavināśinī ।<br>
hṛdayē lalitā dēvī udarē śūladhāriṇī ॥ 29 ॥</p>
<p>nābhau cha kāminī rakṣēdguhyaṃ guhyēśvarī tathā ।<br>
pūtanā kāmikā mēḍhraṃ gudē mahiṣavāhinī ॥ 30 ॥</p>
<p>kaṭyāṃ bhagavatī rakṣējjānunī vindhyavāsinī ।<br>
jaṅghē mahābalā rakṣētsarvakāmapradāyinī ॥ 31 ॥</p>
<p>gulphayōrnārasiṃhī cha pādapṛṣṭhē tu taijasī ।<br>
pādāṅgulīṣu śrī rakṣētpādādhastalavāsinī ॥ 32 ॥</p>
<p>nakhān daṃṣṭrakarālī cha kēśāṃśchaivōrdhvakēśinī ।<br>
rōmakūpēṣu kaumārī tvachaṃ vāgīśvarī tathā ॥ 33 ॥</p>
<p>raktamajjāvasāmāṃsānyasthimēdāṃsi pārvatī ।<br>
antrāṇi kālarātriścha pittaṃ cha mukuṭēśvarī ॥ 34 ॥</p>
<p>padmāvatī padmakōśē kaphē chūḍāmaṇistathā ।<br>
jvālāmukhī nakhajvālāmabhēdyā sarvasandhiṣu ॥ 35 ॥</p>
<p>śukraṃ brahmāṇi! mē rakṣēchChāyāṃ Chatrēśvarī tathā ।<br>
ahaṅkāraṃ manō buddhiṃ rakṣēnmē dharmadhāriṇī ॥ 36 ॥</p>
<p>prāṇāpānau tathā vyānamudānaṃ cha samānakam ।<br>
vajrahastā cha mē rakṣētprāṇaṃ kalyāṇaśōbhanā ॥ 37 ॥</p>
<p>rasē rūpē cha gandhē cha śabdē sparśē cha yōginī ।<br>
sattvaṃ rajastamaśchaiva rakṣēnnārāyaṇī sadā ॥ 38 ॥</p>
<p>āyū rakṣatu vārāhī dharmaṃ rakṣatu vaiṣṇavī ।<br>
yaśaḥ kīrtiṃ cha lakṣmīṃ cha dhanaṃ vidyāṃ cha chakriṇī ॥ 39 ॥</p>
<p>gōtramindrāṇi! mē rakṣētpaśūnmē rakṣa chaṇḍikē ।<br>
putrān rakṣēnmahālakṣmīrbhāryāṃ rakṣatu bhairavī ॥ 40 ॥</p>
<p>panthānaṃ supathā rakṣēnmārgaṃ kṣēmakarī tathā ।<br>
rājadvārē mahālakṣmīrvijayā sarvataḥ sthitā ॥ 41 ॥</p>
<p>rakṣāhīnaṃ tu yat-sthānaṃ varjitaṃ kavachēna tu ।<br>
tatsarvaṃ rakṣa mē dēvi! jayantī pāpanāśinī ॥ 42 ॥</p>
<p>padamēkaṃ na gachChēttu yadīchChēchChubhamātmanaḥ ।<br>
kavachēnāvṛtō nityaṃ yatra yatraiva gachChati ॥ 43 ॥</p>
<p>tatra tatrārthalābhaścha vijayaḥ sārvakāmikaḥ ।<br>
yaṃ yaṃ chintayatē kāmaṃ taṃ taṃ prāpnōti niśchitam ॥ 44 ॥</p>
<p>paramaiśvaryamatulaṃ prāpsyatē bhūtalē pumān ।<br>
nirbhayō jāyatē martyaḥ saṅgrāmēṣvaparājitaḥ ॥ 45 ॥</p>
<p>trailōkyē tu bhavētpūjyaḥ kavachēnāvṛtaḥ pumān ।<br>
idaṃ tu dēvyāḥ kavachaṃ dēvānāmapi durlabham ॥ 46 ॥</p>
<p>yaḥ paṭhētprayatō nityaṃ trisandhyaṃ śraddhayānvitaḥ ।<br>
daivīkalā bhavēttasya trailōkyēṣvaparājitaḥ । 47 ॥</p>
<p>jīvēdvarṣaśataṃ sāgramapamṛtyuvivarjitaḥ ।<br>
naśyanti vyādhayaḥ sarvē lūtāvisphōṭakādayaḥ ॥ 48 ॥</p>
<p>sthāvaraṃ jaṅgamaṃ chaiva kṛtrimaṃ chaiva yadviṣam ।<br>
abhichārāṇi sarvāṇi mantrayantrāṇi bhūtalē ॥ 49 ॥</p>
<p>bhūcharāḥ khēcharāśchaiva julajāśchōpadēśikāḥ ।<br>
sahajā kulajā mālā ḍākinī śākinī tathā ॥ 50 ॥</p>
<p>antarikṣacharā ghōrā ḍākinyaścha mahābalāḥ ।<br>
grahabhūtapiśāchāścha yakṣagandharvarākṣasāḥ ॥ 51 ॥</p>
<p>brahmarākṣasavētālāḥ kūṣmāṇḍā bhairavādayaḥ ।<br>
naśyanti darśanāttasya kavachē hṛdi saṃsthitē ॥ 52 ॥</p>
<p>mānōnnatirbhavēdrājñastējōvṛddhikaraṃ param ।<br>
yaśasā vardhatē sō'pi kīrtimaṇḍitabhūtalē ॥ 53 ॥</p>
<p>japētsaptaśatīṃ chaṇḍīṃ kṛtvā tu kavachaṃ purā ।<br>
yāvadbhūmaṇḍalaṃ dhattē saśailavanakānanam ॥ 54 ॥ </p>
<p>tāvattiṣṭhati mēdinyāṃ santatiḥ putrapautrikī ।<br>
dēhāntē paramaṃ sthānaṃ yatsurairapi durlabham ॥ 55 ॥</p>
<p>prāpnōti puruṣō nityaṃ mahāmāyāprasādataḥ ।<br>
labhatē paramaṃ rūpaṃ śivēna saha mōdatē ॥ 56 ॥</p>
<p>॥ iti vārāhapurāṇē hariharabrahma virachitaṃ dēvyāḥ kavachaṃ sampūrṇam ॥</p>
