<p>mahiṣāsura sainyavadhō nāma dvitīyō'dhyāyaḥ ॥</p>
<p>asya sapta satīmadhyama charitrasya viṣṇur ṛṣiḥ । uṣṇik Chandaḥ । śrīmahālakṣmīdēvatā। śākambharī śaktiḥ । durgā bījam । vāyustattvam । yajurvēdaḥ svarūpam । śrī mahālakṣmīprītyarthē madhyama charitra japē viniyōgaḥ ॥</p>
<p><b>dhyānaṃ</b><br>
ōṃ akṣasrakparaśuṃ gadēṣukuliśaṃ padmaṃ dhanuḥ kuṇḍikāṃ<br>
daṇḍaṃ śaktimasiṃ cha charma jalajaṃ ghaṇṭāṃ surābhājanam ।<br>
śūlaṃ pāśasudarśanē cha dadhatīṃ hastaiḥ pravāḻa prabhāṃ<br>
sēvē sairibhamardinīmiha mahalakṣmīṃ sarōjasthitām ॥</p>
<p>ṛṣiruvācha   ॥1॥</p>
<p>dēvāsuramabhūdyuddhaṃ pūrṇamabdaśataṃ purā।<br>
mahiṣē'surāṇāṃ adhipē dēvānāñcha purandarē            </p>
<p>tatrāsurairmahāvīryirdēvasainyaṃ parājitaṃ।<br>
jitvā cha sakalān dēvān indrō'bhūnmahiṣāsuraḥ   ॥3॥           </p>
<p>tataḥ parājitā dēvāḥ padmayōniṃ prajāpatim।<br>
puraskṛtyagatāstatra yatrēśa garuḍadhvajau     ॥4॥           </p>
<p>yathāvṛttaṃ tayōstadvan mahiṣāsurachēṣṭitam।<br>
tridaśāḥ kathayāmāsurdēvābhibhavavistaram     ॥5॥           </p>
<p>sūryēndrāgnyanilēndūnāṃ yamasya varuṇasya cha<br>
anyēṣāṃ chādhikārānsa svayamēvādhitiṣṭati     ॥6॥           </p>
<p>svargānnirākṛtāḥ sarvē tēna dēva gaṇā bhuviḥ।<br>
vicharanti yathā martyā mahiṣēṇa durātmanā     ॥6॥           </p>
<p>ētadvaḥ kathitaṃ sarvaṃ amarārivichēṣṭitam।<br>
śaraṇaṃ vaḥ prapannāḥ smō vadhastasya vichintyatām     ॥8॥           </p>
<p>itthaṃ niśamya dēvānāṃ vachāṃsi madhusūdhanaḥ<br>
chakāra kōpaṃ śambhuścha bhrukuṭīkuṭilānanau     ॥9॥           </p>
<p>tatō'tikōpapūrṇasya chakriṇō vadanāttataḥ।<br>
niśchakrāma mahattējō brahmaṇaḥ śaṅkarasya cha   ॥10॥           </p>
<p>anyēṣāṃ chaiva dēvānāṃ śakrādīnāṃ śarīrataḥ।<br>
nirgataṃ sumahattējaḥ stachchaikyaṃ samagachChata   ॥11॥           </p>
<p>atīva tējasaḥ kūṭaṃ jvalantamiva parvatam।<br>
dadṛśustē surāstatra jvālāvyāptadigantaram     ॥12॥</p>
<p>atulaṃ tatra tattējaḥ sarvadēva śarīrajam।<br>
ēkasthaṃ tadabhūnnārī vyāptalōkatrayaṃ tviṣā     ॥13॥</p>
<p>yadabhūchChāmbhavaṃ tējaḥ stēnājāyata tanmukham।<br>
yāmyēna chābhavan kēśā bāhavō viṣṇutējasā     ॥14॥</p>
<p>saumyēna stanayōryugmaṃ madhyaṃ chaindrēṇa chābhavat।<br>
vāruṇēna cha jaṅghōrū nitambastējasā bhuvaḥ     ॥15॥</p>
<p>brahmaṇastējasā pādau tadaṅguḻyō'rka tējasā।<br>
vasūnāṃ cha karāṅguḻyaḥ kaubērēṇa cha nāsikā   ॥16॥</p>
<p>tasyāstu dantāḥ sambhūtā prājāpatyēna tējasā<br>
nayanatritayaṃ jajñē tathā pāvakatējasā     ॥17॥</p>
<p>bhruvau cha sandhyayōstējaḥ śravaṇāvanilasya cha<br>
anyēṣāṃ chaiva dēvānāṃ sambhavastējasāṃ śiva    ॥18॥</p>
<p>tataḥ samasta dēvānāṃ tējōrāśisamudbhavām।<br>
tāṃ vilōkya mudaṃ prāpuḥ amarā mahiṣārditāḥ   ॥19॥</p>
<p>śūlaṃ śūlādviniṣkṛṣya dadau tasyai pinākadhṛk।<br>
chakraṃ cha dattavān kṛṣṇaḥ samutpāṭya svachakrataḥ   ॥20॥</p>
<p>śaṅkhaṃ cha varuṇaḥ śaktiṃ dadau tasyai hutāśanaḥ<br>
mārutō dattavāṃśchāpaṃ bāṇapūrṇē tathēṣudhī    ॥21॥</p>
<p>vajramindraḥ samutpāṭya kuliśādamarādhipaḥ।<br>
dadau tasyai sahasrākṣō ghaṇṭāmairāvatādgajāt    ॥22॥</p>
<p>kāladaṇḍādyamō daṇḍaṃ pāśaṃ chāmbupatirdadau।<br>
prajāpatiśchākṣamālāṃ dadau brahmā kamaṇḍalaṃ   ॥23॥</p>
<p>samastarōmakūpēṣu nija raśmīn divākaraḥ<br>
kālaścha dattavān khaḍgaṃ tasyāḥ ścharma cha nirmalam   ॥24॥</p>
<p>kṣīrōdaśchāmalaṃ hāraṃ ajarē cha tathāmbarē<br>
chūḍāmaṇiṃ tathādivyaṃ kuṇḍalē kaṭakānicha     ॥25॥</p>
<p>ardhachandraṃ tadhā śubhraṃ kēyūrān sarva bāhuṣu<br>
nūpurau vimalau tadva dgraivēyakamanuttamam   ॥26॥</p>
<p>aṅguḻīyakaratnāni samastāsvaṅguḻīṣu cha<br>
viśva karmā dadau tasyai paraśuṃ chāti nirmalaṃ   ॥27॥</p>
<p>astrāṇyanēkarūpāṇi tathā'bhēdyaṃ cha daṃśanam।<br>
amlāna paṅkajāṃ mālāṃ śirasyu rasi chāparām॥28॥</p>
<p>adadajjaladhistasyai paṅkajaṃ chātiśōbhanam।<br>
himavān vāhanaṃ siṃhaṃ ratnāni vividhānicha॥29॥</p>
<p>dadāvaśūnyaṃ surayā pānapātraṃ danādhipaḥ।<br>
śēṣaścha sarva nāgēśō mahāmaṇi vibhūṣitam     ॥30॥</p>
<p>nāgahāraṃ dad'u tasyai dhattē yaḥ pṛthivīmimām।<br>
anyairapi surairdēvī bhūṣaṇaiḥ āyudhaistathāḥ     ॥31॥</p>
<p>sammānitā nanādōchchaiḥ sāṭṭahāsaṃ muhurmuhu।<br>
tasyānādēna ghōrēṇa kṛtsna māpūritaṃ nabhaḥ   ॥32॥</p>
<p>amāyatātimahatā pratiśabdō mahānabhūt।<br>
chukṣubhuḥ sakalālōkāḥ samudrāścha chakampirē     ॥33॥</p>
<p>chachāla vasudhā chēluḥ sakalāścha mahīdharāḥ।<br>
jayēti dēvāścha mudā tāmūchuḥ siṃhavāhinīm   ॥34॥</p>
<p>tuṣṭuvurmunayaśchaināṃ bhaktinamrātmamūrtayaḥ।<br>
dṛṣṭvā samastaṃ saṅkṣubdhaṃ trailōkyaṃ amarārayaḥ   ॥35॥</p>
<p>sannaddhākhilasainyāstē samuttasthurudāyudāḥ।<br>
āḥ kimētaditi krōdhādābhāṣya mahiṣāsuraḥ     ॥36॥</p>
<p>abhyadhāvata taṃ śabdaṃ aśēṣairasurairvṛtaḥ।<br>
sa dadarṣa tatō dēvīṃ vyāptalōkatrayāṃ tviṣā॥37॥</p>
<p>pādākrāntyā natabhuvaṃ kirīṭōllikhitāmbarām।<br>
kṣōbhitāśēṣapātāḻāṃ dhanurjyāniḥsvanēna tām     ॥38॥</p>
<p>diśō bhujasahasrēṇa samantādvyāpya saṃsthitām।<br>
tataḥ pravavṛtē yuddhaṃ tayā dēvyā suradviṣāṃ   ॥39॥</p>
<p>śastrāstrairbhahudhā muktairādīpitadigantaram।<br>
mahiṣāsurasēnānīśchikṣurākhyō mahāsuraḥ     ॥40॥</p>
<p>yuyudhē chamaraśchānyaiśchaturaṅgabalānvitaḥ।<br>
rathānāmayutaiḥ ṣaḍbhiḥ rudagrākhyō mahāsuraḥ   ॥41॥</p>
<p>ayudhyatāyutānāṃ cha sahasrēṇa mahāhanuḥ।<br>
pañchāśadbhiścha niyutairasilōmā mahāsuraḥ     ॥42॥</p>
<p>ayutānāṃ śataiḥ ṣaḍbhiḥrbhāṣkalō yuyudhē raṇē।<br>
gajavāji sahasraughai ranēkaiḥ parivāritaḥ   ॥43॥</p>
<p>vṛtō rathānāṃ kōṭyā cha yuddhē tasminnayudhyata।<br>
biḍālākhyō'yutānāṃ cha pañchāśadbhirathāyutaiḥ   ॥44॥</p>
<p>yuyudhē saṃyugē tatra rathānāṃ parivāritaḥ।<br>
anyē cha tatrāyutaśō rathanāgahayairvṛtāḥ     ॥45॥</p>
<p>yuyudhuḥ saṃyugē dēvyā saha tatra mahāsurāḥ।<br>
kōṭikōṭisahastraistu rathānāṃ dantināṃ tathā     ॥46॥</p>
<p>hayānāṃ cha vṛtō yuddhē tatrābhūnmahiṣāsuraḥ।<br>
tōmarairbhindhipālaiścha śaktibhirmusalaistathā   ॥47॥</p>
<p>yuyudhuḥ saṃyugē dēvyā khaḍgaiḥ parasupaṭṭisaiḥ।<br>
kēchichCha chikṣipuḥ śaktīḥ kēchit pāśāṃstathāparē     ॥48॥</p>
<p>dēvīṃ khaḍgaprahāraistu tē tāṃ hantuṃ prachakramuḥ।<br>
sāpi dēvī tatastāni śastrāṇyastrāṇi chaṇḍikā     ॥49॥</p>
<p>līla yaiva prachichChēda nijaśastrāstravarṣiṇī।<br>
anāyastānanā dēvī stūyamānā surarṣibhiḥ     ॥50॥</p>
<p>mumōchāsuradēhēṣu śastrāṇyastrāṇi chēśvarī।<br>
sō'pi kruddhō dhutasaṭō dēvyā vāhanakēsarī     ॥51॥</p>
<p>chachārāsura sainyēṣu vanēṣviva hutāśanaḥ।<br>
niḥśvāsān mumuchēyāṃścha yudhyamānāraṇē'mbikā॥52॥</p>
<p>ta ēva sadhyasambhūtā gaṇāḥ śatasahasraśaḥ।<br>
yuyudhustē paraśubhirbhindipālāsipaṭṭiśaiḥ     ॥53॥</p>
<p>nāśayantō'asuragaṇān dēvīśaktyupabṛṃhitāḥ।<br>
avādayantā paṭahān gaṇāḥ śaṅāṃ stathāparē॥54॥</p>
<p>mṛdaṅgāṃścha tathaivānyē tasminyuddha mahōtsavē।<br>
tatōdēvī triśūlēna gadayā śaktivṛṣṭibhiḥ॥55॥</p>
<p>khaḍgādibhiścha śataśō nijaghāna mahāsurān।<br>
pātayāmāsa chaivānyān ghaṇṭāsvanavimōhitān   ॥56॥</p>
<p>asurān bhuvipāśēna badhvāchānyānakarṣayat।<br>
kēchid dvidhākṛtā stīkṣṇaiḥ khaḍgapātaistathāparē॥57॥</p>
<p>vipōthitā nipātēna gadayā bhuvi śēratē।<br>
vēmuścha kēchidrudhiraṃ musalēna bhṛśaṃ hatāḥ     ॥58॥</p>
<p>kēchinnipatitā bhūmau bhinnāḥ śūlēna vakṣasi।<br>
nirantarāḥ śaraughēna kṛtāḥ kēchidraṇājirē   ॥59॥</p>
<p>śalyānukāriṇaḥ prāṇān mamuchustridaśārdanāḥ।<br>
kēṣāñchidbāhavaśchinnāśchinnagrīvāstathāparē   ॥60॥</p>
<p>śirāṃsi pēturanyēṣāṃ anyē madhyē vidāritāḥ।<br>
vichChinnajajghāsvaparē pētururvyāṃ mahāsurāḥ     ॥61॥</p>
<p>ēkabāhvakṣicharaṇāḥ kēchiddēvyā dvidhākṛtāḥ।<br>
Chinnēpi chānyē śirasi patitāḥ punarutthitāḥ     ॥62॥</p>
<p>kabandhā yuyudhurdēvyā gṛhītaparamāyudhāḥ।<br>
nanṛtuśchāparē tatra yuddē tūryalayāśritāḥ   ॥63॥</p>
<p>kabandhāśchinnaśirasaḥ khaḍgaśakytṛṣṭipāṇayaḥ।<br>
tiṣṭha tiṣṭhēti bhāṣantō dēvī manyē mahāsurāḥ     ॥64॥</p>
<p>pātitai rathanāgāśvaiḥ āsuraiścha vasundharā।<br>
agamyā sābhavattatra yatrābhūt sa mahāraṇaḥ   ॥65॥</p>
<p>śōṇitaughā mahānadyassadyastatra visusruvuḥ।<br>
madhyē chāsurasainyasya vāraṇāsuravājinām     ॥66॥</p>
<p>kṣaṇēna tanmahāsainyamasurāṇāṃ tathā'mbikā।<br>
ninyē kṣayaṃ yathā vahnistṛṇadāru mahāchayam   ॥67॥</p>
<p>sacha siṃhō mahānādamutsṛjan dhutakēsaraḥ।<br>
śarīrēbhyō'marārīṇāmasūniva vichinvati   ॥68॥</p>
<p>dēvyā gaṇaiścha taistatra kṛtaṃ yuddhaṃ tathāsuraiḥ।<br>
yathaiṣāṃ tuṣṭuvurdēvāḥ puṣpavṛṣṭimuchō divi     ॥69॥</p>
<p>jaya jaya  śrī mārkaṇḍēya purāṇē sāvarnikē manvantarē dēvi mahatmyē mahiṣāsurasainyavadhō nāma dvitīyō'dhyāyaḥ॥</p>
