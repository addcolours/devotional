<p>chaṇḍamuṇḍa vadhō nāma saptamōdhyāyaḥ ॥</p>
<p><b>dhyānaṃ</b><br>
dhyāyēṃ ratna pīṭhē śukakala paṭhitaṃ śruṇvatīṃ śyāmalāṅgīṃ।<br>
nyastaikāṅghriṃ sarōjē śaśi śakala dharāṃ vallakīṃ  vāda yantīṃ<br>
kahalārābaddha mālāṃ niyamita vilasachchōlikāṃ rakta vastrāṃ।<br>
mātaṅgīṃ śaṅkha pātrāṃ madhura madhumadāṃ chitrakōdbhāsi bhālāṃ।</p>
<p>ṛṣiruvācha।</p>
<p>ājñaptāstē tatōdaityā-śchaṇḍamuṇḍapurōgamāḥ।<br>
chaturaṅgabalōpētā yayurabhyudyatāyudhāḥ॥1॥</p>
<p>dadṛśustē tatō dēvī-mīṣaddhāsāṃ vyavasthitām।<br>
siṃhasyōpari śailēndra-śṛṅgē mahatikāñchanē॥2॥           </p>
<p>tēdṛṣṭvātāṃsamādātu-mudyamaṃñchakrurudyatāḥ<br>
ākṛṣṭachāpāsidharā-stathā'nyē tatsamīpagāḥ॥3॥           </p>
<p>tataḥ kōpaṃ chakārōchchai-rambikā tānarīnprati।<br>
kōpēna chāsyā vadanaṃ maṣīvarṇamabhūttadā॥4॥           </p>
<p>bhrukuṭīkuṭilāttasyā lalāṭaphalakāddrutam।<br>
kāḻī karāḻa vadanā viniṣkrāntā'sipāśinī   ॥5॥           </p>
<p>vichitrakhaṭvāṅgadharā naramālāvibhūṣaṇā।<br>
dvīpicharmaparīdhānā śuṣkamāṃsā'tibhairavā॥6॥           </p>
<p>ativistāravadanā jihvālalanabhīṣaṇā।<br>
nimagnāraktanayanā nādāpūritadiṅmukhā   ॥6॥           </p>
<p>sā vēgēnā'bhipatitā ghūtayantī mahāsurān।<br>
sainyē tatra surārīṇā-mabhakṣayata tadbalam   ॥8॥           </p>
<p>pārṣṇigrāhāṅkuśagrāhi-yōdhaghaṇṭāsamanvitān।<br>
samādāyaikahastēna mukhē chikṣēpa vāraṇān   ॥9॥           </p>
<p>tathaiva yōdhaṃ turagai rathaṃ sārathinā saha।<br>
nikṣipya vaktrē daśanaiścharvayatyatibhairavaṃ   ॥10॥           </p>
<p>ēkaṃ jagrāha kēśēṣu grīvāyāmatha chāparaṃ।<br>
pādēnākramyachaivānyamurasānyamapōthayat   ॥11॥           </p>
<p>tairmuktānicha śastrāṇi mahāstrāṇi tathāsuraiḥ।<br>
mukhēna jagrāha ruṣā daśanairmathitānyapi ॥12॥</p>
<p>balināṃ tadbalaṃ sarvamasurāṇāṃ durātmanāṃ<br>
mamardābhakṣayachchānyānanyāṃśchātāḍayattathā     ॥13॥</p>
<p>asinā nihatāḥ kēchitkēchitkhaṭvāṅgatāḍitāḥ।<br>
jagmurvināśamasurā dantāgrābhihatāstathā     ॥14॥</p>
<p>kṣaṇēna tadbhalaṃ sarva masurāṇāṃ nipātitaṃ।<br>
dṛṣṭvā chaṇḍō'bhidudrāva tāṃ kāḻīmatibhīṣaṇāṃ   ॥15॥</p>
<p>śaravarṣairmahābhīmairbhīmākṣīṃ tāṃ mahāsuraḥ।<br>
Chādayāmāsa chakraiścha muṇḍaḥ kṣiptaiḥ sahasraśaḥ   ॥16॥</p>
<p>tānichakrāṇyanēkāni viśamānāni tanmukham।<br>
babhuryathārkabimbāni subahūni ghanōdaraṃ     ॥17॥</p>
<p>tatō jahāsātiruṣā bhīmaṃ bhairavanādinī।<br>
kāḻī karāḻavadanā durdarśaśanōjjvalā   ॥18॥</p>
<p>utthāya cha mahāsiṃhaṃ dēvī chaṇḍamadhāvata।<br>
gṛhītvā chāsya kēśēṣu śirastēnāsināchChinat     ॥19॥</p>
<p>atha muṇḍō'bhyadhāvattāṃ dṛṣṭvā chaṇḍaṃ nipātitam।<br>
tamapyapāta yadbhamau sā khaḍgābhihataṃruṣā     ॥20॥</p>
<p>hataśēṣaṃ tataḥ sainyaṃ dṛṣṭvā chaṇḍaṃ nipātitam।<br>
muṇḍañcha sumahāvīryaṃ diśō bhējē bhayāturam   ॥21॥</p>
<p>śiraśchaṇḍasya kāḻī cha gṛhītvā muṇḍa mēva cha।<br>
prāha prachaṇḍāṭṭahāsamiśramabhyētya chaṇḍikām     ॥22॥</p>
<p>mayā tavā trōpahṛtau chaṇḍamuṇḍau mahāpaśū।<br>
yuddhayajñē svayaṃ śumbhaṃ niśumbhaṃ chahaniṣyasi   ॥23॥</p>
<p>ṛṣiruvācha॥</p>
<p>tāvānītau tatō dṛṣṭvā chaṇḍa muṇḍau mahāsurau।<br>
uvācha kāḻīṃ kaḻyāṇī lalitaṃ chaṇḍikā vachaḥ     ॥24॥</p>
<p>yasmāchchaṇḍaṃ cha muṇḍaṃ cha gṛhītvā tvamupāgatā।<br>
chāmuṇḍēti tatō lokē khyātā dēvī bhaviṣyasi     ॥25॥</p>
<p>॥ jaya jaya  śrī mārkaṇḍēya purāṇē sāvarnikē manvantarē dēvi mahatmyē chaṇḍamuṇḍa vadhō nāma saptamōdhyāya samāptam ॥</p>
<p><b>āhuti</b><br>
ōṃ klīṃ jayantī sāṅgāyai saśaktikāyai saparivārāyai savāhanāyai kāḻī chāmuṇḍā dēvyai karpūra bījādhiṣṭhāyai mahāhutiṃ samarpayāmi namaḥ svāhā ॥<br>
