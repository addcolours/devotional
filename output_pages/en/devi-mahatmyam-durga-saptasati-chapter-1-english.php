                  <span style="font-family:NotoSans; line-height:150%;">
<p>॥ dēvī māhātmyam ॥<br>
॥ śrīdurgāyai namaḥ ॥<br>
॥ atha śrīdurgāsaptaśatī ॥<br>
॥ madhukaiṭabhavadhō nāma prathamō'dhyāyaḥ ॥</p>
<p>asya śrī pradhama charitrasya brahmā ṛṣiḥ । mahākāḻī dēvatā । gāyatrī Chandaḥ । nandā śaktiḥ । rakta dantikā bījam । agnistatvam । ṛgvēdaḥ svarūpam । śrī mahākāḻī prītyardhē pradhama charitra japē viniyōgaḥ ।</p>
<p><b>dhyānaṃ</b><br>
khaḍgaṃ chakra gadēṣuchāpa parighā śūlaṃ bhuśuṇḍīṃ śiraḥ<br>
śaṃṅkhaṃ sandadhatīṃ karaistrinayanāṃ sarvāṃṅgabhūṣāvṛtām ।<br>
yāṃ hantuṃ madhukaibhau jalajabhūstuṣṭāva suptē harau<br>
nīlāśmadyuti māsyapādadaśakāṃ sēvē mahākāḻikāṃ॥</p>
<p>ōṃ namaśchaṇḍikāyai<br>
ōṃ aiṃ mārkaṇḍēya uvācha॥1॥</p>
<p>sāvarṇiḥ sūryatanayō yōmanuḥ kathyatē'ṣṭamaḥ।<br>
niśāmaya tadutpattiṃ vistarādgadatō mama   ॥2॥           </p>
<p>mahāmāyānubhāvēna yathā manvantarādhipaḥ<br>
sa babhūva mahābhāgaḥ sāvarṇistanayō ravēḥ   ॥3॥           </p>
<p>svārōchiṣē'ntarē pūrvaṃ chaitravaṃśasamudbhavaḥ।<br>
surathō nāma rājā'bhūt samastē kṣitimaṇḍalē   ॥4॥           </p>
<p>tasya pālayataḥ samyak prajāḥ putrānivaurasān।<br>
babhūvuḥ śatravō bhūpāḥ kōlāvidhvaṃsinastadā   ॥5॥           </p>
<p>tasya tairabhavadyuddhaṃ atiprabaladaṇḍinaḥ।<br>
nyūnairapi sa tairyuddhē kōlāvidhvaṃsibhirjitaḥ   ॥6॥           </p>
<p>tataḥ svapuramāyātō nijadēśādhipō'bhavat।<br>
ākrāntaḥ sa mahābhāgastaistadā prabalāribhiḥ   ॥7॥           </p>
<p>amātyairbalibhirduṣṭai rdurbalasya durātmabhiḥ।<br>
kōśō balaṃ chāpahṛtaṃ tatrāpi svapurē tataḥ   ॥8॥           </p>
<p>tatō mṛgayāvyājēna hṛtasvāmyaḥ sa bhūpatiḥ।<br>
ēkākī hayamāruhya jagāma gahanaṃ vanam   ॥9॥           </p>
<p>satatrāśramamadrākṣī ddvijavaryasya mēdhasaḥ।<br>
praśāntaśvāpadākīrṇa muniśiṣyōpaśōbhitam   ॥10॥          </p>
<p>tasthau kañchitsa kālaṃ cha muninā tēna satkṛtaḥ।<br>
itaśchētaścha vicharaṃstasmin munivarāśramē॥11॥           </p>
<p>sō'chintayattadā tatra mamatvākṛṣṭachētanaḥ।   ॥12॥</p>
<p>matpūrvaiḥ pālitaṃ pūrvaṃ mayāhīnaṃ puraṃ hi tat<br>
madbhṛtyaistairasadvṛttaiḥ rdharmataḥ pālyatē na vā   ॥13॥          </p>
<p>na jānē sa pradhānō mē śūra hastīsadāmadaḥ<br>
mama vairivaśaṃ yātaḥ kānbhōgānupalapsyatē   ॥14॥</p>
<p>yē mamānugatā nityaṃ prasādadhanabhōjanaiḥ<br>
anuvṛttiṃ dhruvaṃ tē'dya kurvantyanyamahībhṛtāṃ   ॥15॥</p>
<p>asamyagvyayaśīlaistaiḥ kurvadbhiḥ satataṃ vyayaṃ<br>
sañchitaḥ sō'tiduḥkhēna kṣayaṃ kōśō gamiṣyati   ॥16॥</p>
<p>ētachchānyachcha satataṃ chintayāmāsa pārthivaḥ<br>
tatra viprāśramābhyāśē vaiśyamēkaṃ dadarśa saḥ   ॥17॥</p>
<p>sa pṛṣṭastēna kastvaṃ bhō hētuścha āgamanē'tra kaḥ<br>
saśōka iva kasmātvaṃ durmanā iva lakṣyasē।   ॥18॥</p>
<p>ityākarṇya vachastasya bhūpatēḥ praṇāyōditam<br>
pratyuvācha sa taṃ vaiśyaḥ praśrayāvanatō nṛpam॥19॥</p>
<p>vaiśya uvācha   ॥20॥</p>
<p>samādhirnāma vaiśyō'hamutpannō dhanināṃ kulē<br>
putradārairnirastaścha dhanalōbhād asādhubhiḥ॥21॥</p>
<p>vihīnaścha dhanaidāraiḥ putrairādāya mē dhanam।<br>
vanamabhyāgatō duḥkhī nirastaśchāptabandhubhiḥ॥22॥</p>
<p>sō'haṃ na vēdmi putrāṇāṃ kuśalākuśalātmikām।<br>
pravṛttiṃ svajanānāṃ cha dārāṇāṃ chātra saṃsthitaḥ॥23॥</p>
<p>kiṃ nu tēṣāṃ gṛhē kṣēmaṃ akṣēmaṃ kinnu sāmprataṃ<br>
kathaṃ tēkinnusadvṛttā durvṛttā kinnumēsutāḥ॥24॥</p>
<p>rājōvācha॥25॥</p>
<p>yairnirastō bhavāँllubdhaiḥ putradārādibhirdhanaiḥ॥26॥</p>
<p>tēṣu kiṃ bhavataḥ snēha manubadhnāti mānasam॥27॥</p>
<p>vaiśya uvācha   ॥28॥</p>
<p>ēvamētadyathā prāha bhavānasmadgataṃ vachaḥ<br>
kiṃ karōmi na badhnāti mama niṣṭuratāṃ manaḥ॥29॥</p>
<p>aiḥ santyajya pitṛsnēhaṃ dhana lubdhairnirākṛtaḥ<br>
patiḥsvajanahārdaṃ cha hārditēṣvēva mē manaḥ।   ॥30॥</p>
<p>kimētannābhijānāmi jānannapi mahāmatē<br>
yatprēma pravaṇaṃ chittaṃ viguṇēṣvapi bandhuṣu॥31॥</p>
<p>tēṣāṃ kṛtē mē niḥśvāsō daurmanasyaṃ chajāyatē॥32॥</p>
<p>arōmi kiṃ yanna manastēṣvaprītiṣu niṣṭhuram   ॥33॥</p>
<p>mārkaṇḍēya uvācha  ॥34॥</p>
<p>tatastau sahitau vipra tammuniṃ samupasthitau॥35॥</p>
<p>samādhirnāma vaiśyō'sau sa cha pārdhiva sattamaḥ॥36॥</p>
<p>kṛtvā tu tau yathānyāyyaṃ yathārhaṃ tēna saṃvidam।<br>
upaviṣṭau kathāḥ kāśchit&ZeroWidthSpace;&ZeroWidthSpace;chchakraturvaiśyapārdhivau॥37॥</p>
<p>rājōvācha   ॥38॥</p>
<p>bhagavṃstvāmahaṃ praṣṭumichChāmyēkaṃ vadasvatat ॥39॥</p>
<p>duḥkhāya yanmē manasaḥ svachittāyattatāṃ vinā॥40॥</p>
<p>mamatvaṃ gatarājyasya rājyāṅgēṣvakhilēṣvapi ।<br>
jānatō'pi yathājñasya kimētanmunisattama ॥ 41 ॥</p>
<p>ayaṃ cha ikṛtaḥ putraiḥ dārairbhṛtyaistathōjghitaḥ<br>
svajanēna cha santyaktaḥ stēṣu hārdī tathāpyati   ॥42॥</p>
<p>ēva mēṣa tathāhaṃ cha dvāvaptyantaduḥkhitau।<br>
dṛṣṭadōṣē'pi viṣayē mamatvākṛṣṭamānasau   ॥43॥</p>
<p>tatkēnaitanmahābhāga yanmōho jñāninōrapi<br>
mamāsya cha bhavatyēṣā vivēkāndhasya mūḍhatā   ॥44॥</p>
<p>ṛṣiruvācha॥45॥</p>
<p>jñāna masti samastasya jantōrvṣaya gōcharē।<br>
viṣayaścha mahābhāga yānti chaivaṃ pṛthakpṛthak॥46॥</p>
<p>kēchiddivā tathā rātrau prāṇinaḥ stulyadṛṣṭayaḥ   ॥47॥</p>
<p>jñāninō manujāḥ satyaṃ kiṃ tu tē na hi kēvalam।<br>
yatō hi jñāninaḥ sarvē paśupakṣimṛgādayaḥ॥48॥</p>
<p>jñānaṃ cha tanmanuṣyāṇāṃ yattēṣāṃ mṛgapakṣiṇāṃ<br>
manuṣyāṇāṃ cha yattēṣāṃ tulyamanyattathōbhayōḥ॥49॥</p>
<p>jñānē'pi sati paśyaitān patagāñChābachañchuṣu।<br>
kaṇamōkṣādṛtān mōhātpīḍyamānānapi kṣudhā॥50॥</p>
<p>mānuṣā manujavyāghra sābhilāṣāḥ sutān prati<br>
lōbhāt pratyupakārāya nanvētān kiṃ na paśyasi॥51॥</p>
<p>tathāpi mamatāvartē mōhagartē nipātitāḥ<br>
mahāmāyā prabhāvēṇa saṃsārasthitikāriṇā॥52॥</p>
<p>tannātra vismayaḥ kāryō yōganidrā jagatpatēḥ।<br>
mahāmāyā harēśchaiṣā tayā sammōhyatē jagat॥53॥</p>
<p>jṅānināmapi chētāṃsi dēvī bhagavatī hi sā<br>
balādākRṣyamōhāya mahāmāyā prayachChati   ॥54॥</p>
<p>tayā visṛjyatē viśvaṃ jagadētachcharācharam ।<br>
saiṣā prasannā varadā nṛṇāṃ bhavati muktayē  ॥55॥</p>
<p>sā vidyā paramā muktērhētubhūtā sanātanī<br>
saṃsārabandhahētuścha saiva sarvēśvarēśvarī॥56॥</p>
<p>rājōvācha॥57॥</p>
<p>bhagavan kāhi sā dēvī māmāyēti yāṃ bhavān ।<br>
bravīti kthamutpannā sā karmāsyāścha kiṃ dvija॥58॥</p>
<p>yatprabhāvā cha sā dēvī yatsvarūpā yadudbhavā।<br>
tatsarvaṃ śrōtumichChāmi tvattō brahmavidāṃ vara॥59॥</p>
<p>ṛṣiruvācha   ॥60॥</p>
<p>nityaiva sā jaganmūrtistayā sarvamidaṃ tatam॥61॥</p>
<p>tathāpi tatsamutpattirbahudhā śrūyatāṃ mamaḥ॥62॥</p>
<p>dēvānāṃ kāryasiddhyarthaṃ āvirbhavati sā yadā।<br>
utpannēti tadā lōkē sā nityāpyabhidhīyatē  ॥63॥</p>
<p>yōganidrāṃ yadā viṣṇurjagatyēkārṇavīkṛtē।<br>
āstīrya śēṣamabhajat kalpāntē bhagavān prabhuḥ॥64॥</p>
<p>tadā dvāvasurau ghōrau vikhyātau madhukaiṭabhau।<br>
viṣṇukarṇamalōdbhūtau hantuṃ brahmāṇamudyatau॥65॥</p>
<p>sa nābhi kamalē viṣṇōḥ sthitō brahmā prajāpatiḥ<br>
dṛṣṭvā tāvasurau chōgrau prasuptaṃ cha janārdanam॥66॥</p>
<p>tuṣṭāva yōganidrāṃ tāmēkāgrahṛdayaḥ sthitaḥ<br>
vibōdhanārdhāya harērharinētrakṛtālayām   ॥67॥</p>
<p>viśvēśvarīṃ jagaddhātrīṃ sthitisaṃhārakāriṇīm।<br>
nidrāṃ bhagavatīṃ viṣṇōratulāṃ tējasaḥ prabhuḥ  ॥68॥</p>
<p>brahmōvācha   ॥69॥</p>
<p>tvaṃ svāhā tvaṃ svadhā tvaṃhi vaṣaṭkāraḥ svarātmikā।<br>
sudhā tvamakṣarē nityē tridhā mātrātmikā sthitā॥70॥</p>
<p>ardhamātrā sthitā nityā yānuchchāryāviśēṣataḥ<br>
tvamēva sā tvaṃ sāvitrī tvaṃ dēva jananī parā  ॥71॥</p>
<p>tvayaitaddhāryatē viśvaṃ tvayaitat sṛjyatē jagat।<br>
tvayaitat pālyatē dēvi tvamatsyantē cha sarvadā॥72॥</p>
<p>visṛṣṭau sṛṣṭirūpātvaṃ sthiti rūpā cha pālanē।<br>
tathā saṃhṛtirūpāntē jagatō'sya jaganmayē  ॥73॥</p>
<p>mahāvidyā mahāmāyā mahāmēdhā mahāsmṛtiḥ।<br>
mahāmōhā cha bhavatī mahādēvī mahāsurī   ॥74॥</p>
<p>prakṛtistvaṃ cha sarvasya guṇatraya vibhāvinī।<br>
kāḻarātrirmahārātrirmōharātriścha dāruṇā॥75॥</p>
<p>tvaṃ śrīstvamīśvarī tvaṃ hrīstvaṃ buddhirbhōdhalakṣaṇā।<br>
lajjāpuṣṭistathā tuṣṭistvaṃ śāntiḥ kṣānti rēva cha॥76॥</p>
<p>khaḍginī śūlinī ghōrā gadinī chakriṇī tathā।<br>
śaṅkhiṇī chāpinī bāṇābhuśuṇḍīparighāyudhā॥77॥</p>
<p>saumyā saumyatarāśēṣasaumyēbhyastvatisundarī<br>
parāparāṇāṃ paramā tvamēva paramēśvarī॥78॥</p>
<p>yachcha kiñchitkvachidvastu sadasadvākhilātmikē।<br>
tasya sarvasya yā śaktiḥ sā tvaṃ kiṃ stūyasēmayā॥79॥</p>
<p>yayā tvayā jagat sraṣṭā jagatpātātti yō jagat।<br>
sō'pi nidrāvaśaṃ nītaḥ kastvāṃ stōtumihēśvaraḥ॥80॥</p>
<p>viṣṇuḥ śarīragrahaṇaṃ ahamīśāna ēva cha<br>
kāritāstē yatō'tastvāṃ kaḥ stōtuṃ śaktimān bhavēt॥81॥</p>
<p>sā tvamitthaṃ prabhāvaiḥ svairudārairdēvi saṃstutā।<br>
mōhayaitau durādharṣāvasurau madhukaiṭabhau    ॥82॥</p>
<p>prabōdhaṃ cha jagatsvāmī nīyatāmachyutā laghu   ॥83॥<br>
bōdhaścha kriyatāmasya hantumētau mahāsurau     ॥83॥</p>
<p>ṛṣiruvācha   ॥84॥</p>
<p>ēvaṃ stutā tadā dēvī tāmasī tatra vēdhasā<br>
viṣṇōḥ prabhōdhanārdhāya nihantuṃ madhukaiṭabhau     ॥85॥</p>
<p>nētrāsyanāsikābāhuhṛdayēbhyastathōrasaḥ।<br>
nirgamya darśanē tasthau brahmaṇō avyaktajanmanaḥ     ॥86॥</p>
<p>uttasthau cha jagannāthaḥ stayā muktō janārdanaḥ।<br>
ēkārṇavē ahiśayanāttataḥ sa dadṛśē cha tau     ॥87॥</p>
<p>madhukaiṭabhau durātmānā vativīryaparākramau<br>
krōdharaktēkṣaṇāvattuṃ brahmaṇāṃ janitōdyamau     ॥88॥</p>
<p>samutthāya tatastābhyāṃ yuyudhē bhagavān hariḥ<br>
pañchavarṣasahastrāṇi bāhupraharaṇō vibhuḥ     ॥89॥</p>
<p>tāvapyatibalōnmattau mahāmāyāvimōhitau     ॥90॥</p>
<p>uktavantau varō'smattō vriyatāmiti kēśavam     ॥91॥</p>
<p>śrī bhagavānuvācha   ॥92॥</p>
<p>bhavētāmadya mē tuṣṭau mama vadhyāvubhāvapi     ॥93॥</p>
<p>kimanyēna varēṇātra ētāvṛddi vṛtaṃ mama    ॥94॥</p>
<p>ṛṣiruvācha   ॥95॥</p>
<p>vañchitābhyāmiti tadā sarvamāpōmayaṃ jagat।<br>
vilōkya tābhyāṃ gaditō bhagavān kamalēkṣaṇaḥ     ॥96॥</p>
<p>āvāṃ jahi na yatrōrvī salilēna pariplutā।     ॥97॥</p>
<p>ṛṣiruvācha   ॥98॥</p>
<p>tathētyuktvā bhagavatā śaṅkhachakragadābhṛtā।<br>
kṛtvā chakrēṇa vai Chinnē jaghanē śirasī tayōḥ     ॥99॥</p>
<p>ēvamēṣā samutpannā brahmaṇā saṃstutā svayam।<br>
prabhāvamasyā dēvyāstu bhūyaḥ śṛṇu vadāmi tē   ॥100॥</p>
<p>॥ jaya jaya śrī svasti śrīmārkaṇḍēyapurāṇē sāvarṇikē manvantarē dēvīmahātmyē madhukaiṭabhavadhō nāma pradhamō'dhyāyaḥ ॥</p>
<p><b>āhuti</b></p>
<p>ōṃ ēṃ sāṅgāyai sāyudhāyai saśaktikāyai saparivārāyai savāhanāyai ēṃ bījādhiṣṭāyai mahā kāḻikāyai mahā ahutiṃ samarpayāmi namaḥ svāhā ॥</p>
