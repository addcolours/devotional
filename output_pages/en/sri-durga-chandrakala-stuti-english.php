tāmādidēvataruṇīṃ praṇamāmi mūrdhnā ॥ 8 ॥</p>
<p>māhiṣmatītanubhavaṃ cha ruruṃ cha hantuṃ<br>
āviṣkṛtairnijarasādavatārabhēdaiḥ ।<br>
