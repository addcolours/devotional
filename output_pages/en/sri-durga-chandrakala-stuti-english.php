ambā sadā samabhirakṣatu māṃ vipadbhyaḥ ॥ 9 ॥</p>
<p>ētachcharitramakhilaṃ likhitaṃ hi yasyāḥ<br>
sampūjitaṃ sadana ēva nivēśitaṃ vā ।<br>
durgaṃ cha tārayati dustaramapyaśēṣaṃ<br>
śrēyaḥ prayachChati cha sarvamumāṃ bhajē tām ॥ 10 ॥</p>
<p>yatpūjanastutinamaskṛtibhirbhavanti<br>
