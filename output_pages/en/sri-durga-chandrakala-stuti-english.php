<p><b>dhyānam</b><br>
umākāntē ramākāntē ēṣāmāsī-nmatissamā ।<br>
namāmi dīkṣitēndrāṃ stān nayaṣaṭka-viśāradām ॥ </p>
<p>vēdhōharīśvarastutyāṃ vihartrīṃ vindhyabhūdharē ।<br>
haraprāṇēśvarīṃ vandē hantrīṃ vibudhavidviṣām ॥ 1 ॥</p>
<p>abhyarthanēna sarasīruhasambhavasya<br>
tyaktvōditā bhagavadakṣipidhānalīlām ।<br>
viśvēśvarī vipadapākaraṇē purastāt<br>
mātā mamāstu madhukaiṭabhayōrnihantrī ॥ 2 ॥</p>
<p>prāṅnirjarēṣu nihatairnijaśaktilēśaiḥ<br>
ēkībhavadbhiruditā'khilalōkaguptyai ।<br>
sampannaśastranikarā cha tadāyudhasthaiḥ<br>
mātā mamāstu mahiṣāntakarī purastāt ॥ 3 ॥</p>
<p>prālēyaśailatanayā tanukāntisampat<br>
kōśōditā kuvalayachChavichārudēhā ।<br>
nārāyaṇī namadabhīpsitakalpavallī<br>
suprītimāvahatu śumbhaniśumbhahantrī ॥ 4 ॥</p>
<p>viśvēśvarīti mahiṣāntakarīti yasyāḥ<br>
nārāyaṇītyapi cha nāmabhiraṅkitāni ।<br>
sūktāni paṅkajabhuvā cha surarṣibhiścha<br>
dṛṣṭāni pāvakamukhaiścha śivāṃ bhajē tām ॥ 5 ॥</p>
<p>utpattidaityahananastavanātmakāni<br>
saṃrakṣakāṇyakhilabhūtahitāya yasyāḥ ।<br>
sūktānyaśēṣanigamāntavidaḥ paṭhanti<br>
tāṃ viśvamātaramajasramabhiṣṭavīmi ॥ 6 ॥</p>
<p>yē vaiprachittapunarutthitaśumbhamukhyaiḥ<br>
durbhikṣaghōrasamayēna cha kāritāsu ।<br>
āviṣkṛtāstrijagadārtiṣu rūpabhēdāḥ<br>
tairambikā samabhirakṣatu māṃ vipadbhyaḥ ॥ 7 ॥</p>
<p>sūktaṃ yadīyamaravindabhavādi dṛṣṭaṃ<br>
āvartya dēvyanupadaṃ surathaḥ samādhiḥ ।<br>
