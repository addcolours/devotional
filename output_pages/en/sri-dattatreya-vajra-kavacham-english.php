<p>ṛṣaya ūchuḥ ।<br/>
kathaṃ saṅkalpasiddhiḥ syādvēdavyāsa kalauyugē ।<br/>
dharmārthakāmamōkṣāṇāṃ sādhanaṃ kimudāhṛtam ॥ 1 ॥</p>
<p>vyāsa uvācha ।<br/>
śṛṇvantu ṛṣayassarvē śīghraṃ saṅkalpasādhanam ।<br/>
sakṛduchchāramātrēṇa bhōgamōkṣapradāyakam ॥ 2 ॥</p>
<p>gaurīśṛṅgē himavataḥ kalpavṛkṣōpaśōbhitam ।<br/>
dīptē divyamahāratna hēmamaṇḍapamadhyagam ॥ 3 ॥</p>
<p>ratnasiṃhāsanāsīnaṃ prasannaṃ paramēśvaram ।<br/>
mandasmitamukhāmbhōjaṃ śaṅkaraṃ prāha pārvatī ॥ 4 ॥</p>
<p>śrīdēvī uvācha ।<br/>
dēvadēva mahādēva lōkaśaṅkara śaṅkara ।<br/>
mantrajālāni sarvāṇi yantrajālāni kṛtsnaśaḥ ॥ 5 ॥</p>
<p>tantrajālānyanēkāni mayā tvattaḥ śrutāni vai ।<br/>
idānīṃ draṣṭumichChāmi viśēṣēṇa mahītalam ॥ 6 ॥</p>
<p>ityudīritamākarṇya pārvatyā paramēśvaraḥ ।<br/>
karēṇāmṛjya santōṣāt pārvatīṃ pratyabhāṣata ॥ 7 ॥</p>
<p>mayēdānīṃ tvayā sārdhaṃ vṛṣamāruhya gamyatē ।<br/>
ityuktvā vṛṣamāruhya pārvatyā saha śaṅkaraḥ ॥ 8 ॥</p>
<p>yayau bhūmaṇḍalaṃ draṣṭuṃ gauryāśchitrāṇi darśayan ।<br/>
kvachit vindhyāchalaprāntē mahāraṇyē sudurgamē ॥ 9 ॥</p>
<p>tatra vyāhartumāyāntaṃ bhillaṃ paraśudhāriṇam ।<br/>
vadhyamānaṃ mahāvyāghraṃ nakhadaṃṣṭrābhirāvṛtam ॥ 10 ॥</p>
<p>atīva chitrachāritryaṃ vajrakāyasamāyutam ।<br/>
aprayatnamanāyāsamakhinnaṃ sukhamāsthitam ॥ 11 ॥</p>
<p>palāyantaṃ mṛgaṃ paśchādvyāghrō bhītyā palāyataḥ ।<br/>
ētadāścharyamālōkya pārvatī prāha śaṅkaram ॥ 12 ॥</p>
<p>śrī pārvatyuvācha ।<br/>
kimāścharyaṃ kimāścharyamagrē śambhō nirīkṣyatām ।<br/>
ityuktaḥ sa tataḥ śambhurdṛṣṭvā prāha purāṇavit ॥ 13 ॥</p>
<p>śrī śaṅkara uvācha ।<br/>
gauri vakṣyāmi tē chitramavāṅmānasagōcharam ।<br/>
adṛṣṭapūrvamasmābhirnāsti kiñchinna kutrachit ॥ 14 ॥</p>
<p>mayā samyak samāsēna vakṣyatē śṛṇu pārvati ।<br/>
ayaṃ dūraśravā nāma bhillaḥ paramadhārmikaḥ ॥ 15 ॥</p>
<p>samitkuśaprasūnāni kandamūlaphalādikam ।<br/>
pratyahaṃ vipinaṃ gatvā samādāya prayāsataḥ ॥ 16 ॥</p>
<p>priyē pūrvaṃ munīndrēbhyaḥ prayachChati na vāñChati ।<br/>
tē'pi tasminnapi dayāṃ kurvatē sarvamauninaḥ ॥ 17 ॥</p>
<p>dalādanō mahāyōgī vasannēva nijāśramē ।<br/>
kadāchidasmarat siddhaṃ dattātrēyaṃ digambaram ॥ 18 ॥</p>
<p>dattātrēyaḥ smartṛgāmī chētihāsaṃ parīkṣitum ।<br/>
tat‍kṣaṇāt sō'pi yōgīndrō dattātrēyaḥ samutthitaḥ ॥ 19 ॥</p>
<p>taṃ dṛṣṭvāścharyatōṣābhyāṃ dalādanamahāmuniḥ ।<br/>
sampūjyāgrē viṣīdantaṃ dattātrēyamuvācha tam ॥ 20 ॥</p>
<p>mayōpahūtaḥ samprāptō dattātrēya mahāmunē ।<br/>
smartṛgāmī tvamityētat kiṃ vadantī parīkṣitum ॥ 21 ॥</p>
<p>mayādya saṃsmṛtō'si tvamaparādhaṃ kṣamasva mē ।<br/>
dattātrēyō muniṃ prāha mama prakṛtirīdṛśī ॥ 22 ॥</p>
<p>abhaktyā vā subhaktyā vā yaḥ smarēnnāmananyadhīḥ ।<br/>
tadānīṃ tamupāgamya dadāmi tadabhīpsitam ॥ 23 ॥</p>
<p>dattātrēyō muniṃ prāha dalādanamunīśvaram ।<br/>
yadiṣṭaṃ tadvṛṇīṣva tvaṃ yat prāptō'haṃ tvayā smṛtaḥ ॥ 24 ॥</p>
<p>dattātrēyaṃ muniṃ prāha mayā kimapi nōchyatē ।<br/>
tvachchittē yat sthitaṃ tanmē prayachCha munipuṅgava ॥ 25 ॥</p>
<p>śrī dattātrēya uvācha ।<br/>
mamāsti vajrakavachaṃ gṛhāṇētyavadanmunim ।<br/>
tathētyaṅgīkṛtavatē dalādamunayē muniḥ ॥ 26 ॥</p>
<p>svavajrakavachaṃ prāha ṛṣichChandaḥ purassaram ।<br/>
nyāsaṃ dhyānaṃ phalaṃ tatra prayōjanamaśēṣataḥ ॥ 27 ॥</p>
<p>asya śrīdattātrēya vajrakavacha stōtramantrasya, kirātarūpī mahārudrṛṣiḥ, anuṣṭup Chandaḥ, śrīdattātrēyō dēvatā, drāṃ bījam, āṃ śaktiḥ, krauṃ kīlakam.<br/>
ōṃ ātmanē namaḥ<br/>
ōṃ drīṃ manasē namaḥ<br/>
ōṃ āṃ drīṃ śrīṃ sauḥ<br/>
ōṃ klāṃ klīṃ klūṃ klaiṃ klauṃ klaḥ<br/>
śrī dattātrēya prasāda siddhyarthē japē viniyōgaḥ</p>
<p>karanyāsaḥ ।<br/>
ōṃ drāṃ aṅguṣṭhābhyāṃ namaḥ ।<br/>
ōṃ drīṃ tarjanībhyāṃ namaḥ ।<br/>
ōṃ drūṃ madhyamābhyāṃ namaḥ ।<br/>
ōṃ draiṃ anāmikābhyāṃ namaḥ ।<br/>
ōṃ drauṃ kaniṣṭhikābhyāṃ namaḥ ।<br/>
ōṃ draḥ karatalakarapṛṣṭhābhyāṃ namaḥ ।</p>
<p>hṛdayādinyāsaḥ ।<br/>
ōṃ drāṃ hṛdayāya namaḥ ।<br/>
ōṃ drīṃ śirasē svāhā ।<br/>
ōṃ drūṃ śikhāyai vaṣaṭ ।<br/>
ōṃ draiṃ kavachāya hum ।<br/>
ōṃ drauṃ nētratrayāya vauṣaṭ ।<br/>
ōṃ draḥ astrāya phaṭ ।<br/>
ōṃ bhūrbhuvassuvarōmiti digbandhaḥ ।</p>
<p>dhyānam ।<br/>
jagadaṅkurakandāya sachchidānandamūrtayē ।<br/>
dattātrēyāya yōgīndrachandrāya paramātmanē ॥ 1 ॥</p>
<p>kadā yōgī kadā bhōgī kadā nagnaḥ piśāchavat ।<br/>
dattātrēyō hariḥ sākṣāt bhuktimuktipradāyakaḥ ॥ 2 ॥</p>
<p>vārāṇasīpurasnāyī kolhāpurajapādaraḥ ।<br/>
māhurīpurabhīkṣāśī sahyaśāyī digambaraḥ ॥ 3 ॥</p>
<p>indranīla samākāraḥ chandrakāntisamadyutiḥ ।<br/>
vaiḍhūrya sadṛśasphūrtiḥ chalatkiñchijjaṭādharaḥ ॥ 4 ॥</p>
<p>snigdhadhāvalya yuktākṣō'tyantanīla kanīnikaḥ ।<br/>
bhrūvakṣaḥśmaśrunīlāṅkaḥ śaśāṅkasadṛśānanaḥ ॥ 5 ॥</p>
<p>hāsanirjita nihāraḥ kaṇṭhanirjita kambukaḥ ।<br/>
māṃsalāṃsō dīrghabāhuḥ pāṇinirjitapallavaḥ ॥ 6 ॥</p>
<p>viśālapīnavakṣāścha tāmrapāṇirdalōdaraḥ ।<br/>
pṛthulaśrōṇilalitō viśālajaghanasthalaḥ ॥ 7 ॥</p>
<p>rambhāstambhōpamānōruḥ jānupūrvaikajaṅghakaḥ ।<br/>
gūḍhagulphaḥ kūrmapṛṣṭhō lasatvādōparisthalaḥ ॥ 8 ॥</p>
<p>raktāravindasadṛśa ramaṇīya padādharaḥ ।<br/>
charmāmbaradharō yōgī smartṛgāmī kṣaṇēkṣaṇē ॥ 9 ॥</p>
<p>jñānōpadēśaniratō vipaddharaṇadīkṣitaḥ ।<br/>
siddhāsanasamāsīna ṛjukāyō hasanmukhaḥ ॥ 10 ॥</p>
<p>vāmahastēna varadō dakṣiṇēnābhayaṅkaraḥ ।<br/>
bālōnmatta piśāchībhiḥ kvachid yuktaḥ parīkṣitaḥ ॥ 11 ॥</p>
<p>tyāgī bhōgī mahāyōgī nityānandō nirañjanaḥ ।<br/>
sarvarūpī sarvadātā sarvagaḥ sarvakāmadaḥ ॥ 12 ॥</p>
<p>bhasmōddhūḻita sarvāṅgō mahāpātakanāśanaḥ ।<br/>
bhuktipradō muktidātā jīvanmuktō na saṃśayaḥ ॥ 13 ॥</p>
<p>ēvaṃ dhyātvā'nanyachittō madvajrakavachaṃ paṭhēt ।<br/>
māmēva paśyansarvatra sa mayā saha sañcharēt ॥ 14 ॥</p>
<p>digambaraṃ bhasmasugandha lēpanaṃ<br/>
chakraṃ triśūlaṃ ḍhamaruṃ gadāyudham ।<br/>
padmāsanaṃ yōgimunīndravanditaṃ<br/>
dattētināmasmaraṇēna nityam ॥ 15 ॥</p>
<p>pañchōpachārapūjā ।</p>
<p>ōṃ laṃ pṛthivītattvātmanē śrīdattātrēyāya namaḥ ।<br/>
gandhaṃ parikalpayāmi।</p>
<p>ōṃ haṃ ākāśatattvātmanē śrīdattātrēyāya namaḥ ।<br/>
puṣpaṃ parikalpayāmi ।</p>
<p>ōṃ yaṃ vāyutattvātmanē śrīdattātrēyāya namaḥ ।<br/>
dhūpaṃ parikalpayāmi ।</p>
<p>ōṃ raṃ vahnitattvātmanē śrīdattātrēyāya namaḥ ।<br/>
dīpaṃ parikalpayāmi ।</p>
<p>ōṃ vaṃ amṛta tattvātmanē śrīdattātrēyāya namaḥ ।<br/>
amṛtanaivēdyaṃ parikalpayāmi ।</p>
<p>ōṃ saṃ sarvatattvātmanē śrīdattātrēyāya namaḥ ।<br/>
tāmbūlādisarvōpachārān parikalpayāmi ।</p>
<p>(anantaraṃ ‘ōṃ drāṃ…’ iti mūlamantraṃ aṣṭōttaraśatavāraṃ (108) japēt)</p>
<p>atha vajrakavacham ।</p>
<p>ōṃ dattātrēyāya śiraḥpātu sahasrābjēṣu saṃsthitaḥ ।<br/>
phālaṃ pātvānasūyēyaḥ chandramaṇḍalamadhyagaḥ ॥ 1 ॥</p>
<p>kūrchaṃ manōmayaḥ pātu haṃ kṣaṃ dvidalapadmabhūḥ ।<br/>
jyōtirūpō'kṣiṇīpātu pātu śabdātmakaḥ śrutī ॥ 2 ॥</p>
<p>nāsikāṃ pātu gandhātmā mukhaṃ pātu rasātmakaḥ ।<br/>
jihvāṃ vēdātmakaḥ pātu dantōṣṭhau pātu dhārmikaḥ ॥ 3 ॥</p>
<p>kapōlāvatribhūḥ pātu pātvaśēṣaṃ mamātmavit ।<br/>
sarvātmā ṣōḍaśārābjasthitaḥ svātmā'vatād galam ॥ 4 ॥</p>
<p>skandhau chandrānujaḥ pātu bhujau pātu kṛtādibhūḥ ।<br/>
jatruṇī śatrujit pātu pātu vakṣasthalaṃ hariḥ ॥ 5 ॥</p>
<p>kādiṭhāntadvādaśārapadmagō marudātmakaḥ ।<br/>
yōgīśvarēśvaraḥ pātu hṛdayaṃ hṛdayasthitaḥ ॥ 6 ॥</p>
<p>pārśvē hariḥ pārśvavartī pātu pārśvasthitaḥ smṛtaḥ ।<br/>
haṭhayōgādiyōgajñaḥ kukṣiṃ pātu kṛpānidhiḥ ॥ 7 ॥</p>
<p>ḍakārādi phakārānta daśārasarasīruhē ।<br/>
nābhisthalē vartamānō nābhiṃ vahnyātmakō'vatu ॥ 8 ॥</p>
<p>vahnitattvamayō yōgī rakṣatānmaṇipūrakam ।<br/>
kaṭiṃ kaṭisthabrahmāṇḍavāsudēvātmakō'vatu ॥ 9 ॥</p>
<p>vakārādi lakārānta ṣaṭpatrāmbujabōdhakaḥ ।<br/>
jalatattvamayō yōgī svādhiṣṭhānaṃ mamāvatu ॥ 10 ॥</p>
<p>siddhāsana samāsīna ūrū siddhēśvarō'vatu ।<br/>
vādisānta chatuṣpatrasarōruha nibōdhakaḥ ॥ 11 ॥</p>
<p>mūlādhāraṃ mahīrūpō rakṣatād vīryanigrahī ।<br/>
pṛṣṭhaṃ cha sarvataḥ pātu jānunyastakarāmbujaḥ ॥ 12 ॥</p>
<p>jaṅghē pātvavadhūtēndraḥ pātvaṅghrī tīrthapāvanaḥ ।<br/>
sarvāṅgaṃ pātu sarvātmā rōmāṇyavatu kēśavaḥ ॥ 13 ॥</p>
<p>charma charmāmbaraḥ pātu raktaṃ bhaktipriyō'vatu ।<br/>
māṃsaṃ māṃsakaraḥ pātu majjāṃ majjātmakō'vatu ॥ 14 ॥</p>
<p>asthīni sthiradhīḥ pāyānmēdhāṃ vēdhāḥ prapālayēt ।<br/>
śukraṃ sukhakaraḥ pātu chittaṃ pātu dṛḍhākṛtiḥ ॥ 15 ॥</p>
<p>manōbuddhimahaṅkāraṃ hṛṣīkēśātmakō'vatu ।<br/>
karmēndriyāṇi pātvīśaḥ pātu jñānēndriyāṇyajaḥ ॥ 16 ॥</p>
<p>bandhūn bandhūttamaḥ pāyāchChatrubhyaḥ pātu śatrujit ।<br/>
gṛhārāmadhanakṣētraputrādīn śaṅkarō'vatu ॥ 17 ॥</p>
<p>bhāryāṃ prakṛtivit pātu paśvādīn pātu śār‍ṅgabhṛt ।<br/>
prāṇān pātu pradhānajñō bhakṣyādīn pātu bhāskaraḥ ॥ 18 ॥</p>
<p>sukhaṃ chandrātmakaḥ pātu duḥkhāt pātu purāntakaḥ ।<br/>
paśūn paśupatiḥ pātu bhūtiṃ bhūtēśvarō mama ॥ 19 ॥</p>
<p>prāchyāṃ viṣaharaḥ pātu pātvāgnēyyāṃ makhātmakaḥ ।<br/>
yāmyāṃ dharmātmakaḥ pātu nairṛtyāṃ sarvavairihṛt ॥ 20 ॥</p>
<p>varāhaḥ pātu vāruṇyāṃ vāyavyāṃ prāṇadō'vatu ।<br/>
kaubēryāṃ dhanadaḥ pātu pātvaiśānyāṃ mahāguruḥ ॥ 21 ॥</p>
<p>ūrdhvaṃ pātu mahāsiddhaḥ pātvadhastājjaṭādharaḥ ।<br/>
rakṣāhīnaṃ tu yat sthānaṃ rakṣatvādimunīśvaraḥ ॥ 22 ॥</p>
<p>karanyāsaḥ ।<br/>
ōṃ drāṃ aṅguṣṭhābhyāṃ namaḥ ।<br/>
ōṃ drīṃ tarjanībhyāṃ namaḥ ।<br/>
ōṃ drūṃ madhyamābhyāṃ namaḥ ।<br/>
ōṃ draiṃ anāmikābhyāṃ namaḥ ।<br/>
ōṃ drauṃ kaniṣṭhikābhyāṃ namaḥ ।<br/>
ōṃ draḥ karatalakarapṛṣṭhābhyāṃ namaḥ ।</p>
<p>hṛdayādinyāsaḥ ।<br/>
ōṃ drāṃ hṛdayāya namaḥ ।<br/>
ōṃ drīṃ śirasē svāhā ।<br/>
ōṃ drūṃ śikhāyai vaṣaṭ ।<br/>
ōṃ draiṃ kavachāya hum ।<br/>
ōṃ drauṃ nētratrayāya vauṣaṭ ।<br/>
ōṃ draḥ astrāya phaṭ ।<br/>
ōṃ bhūrbhuvassuvarōmiti digvimōkaḥ ।</p>
<p>phalaśṛti ॥</p>
<p>ētanmē vajrakavachaṃ yaḥ paṭhēt śṛṇuyādapi ।<br/>
vajrakāyaśchirañjīvī dattātrēyō'hamabruvam ॥ 23 ॥</p>
<p>tyāgī bhōgī mahāyōgī sukhaduḥkhavivarjitaḥ ।<br/>
sarvatra siddhasaṅkalpō jīvanmuktō'dyavartatē ॥ 24 ॥</p>
<p>ityuktvāntardadhē yōgī dattātrēyō digambaraḥ ।<br/>
dalādanō'pi tajjaptvā jīvanmuktaḥ sa vartatē ॥ 25 ॥</p>
<p>bhillō dūraśravā nāma tadānīṃ śrutavānidam ।<br/>
sakṛchChravaṇamātrēṇa vajrāṅgō'bhavadapyasau ॥ 26 ॥</p>
<p>ityētad vajrakavachaṃ dattātrēyasya yōginaḥ ।<br/>
śrutvā śēṣaṃ śambhumukhāt punarapyāha pārvatī ॥ 27 ॥</p>
<p>śrī pārvatyuvācha ।</p>
<p>ētat kavacha māhātmyaṃ vada vistaratō mama ।<br/>
kutra kēna kadā jāpyaṃ kiyajjāpyaṃ kathaṃ katham ॥ 28 ॥</p>
<p>uvācha śambhustat sarvaṃ pārvatyā vinayōditam ।</p>
<p>śrīparamēśvara uvācha ।</p>
<p>śṛṇu pārvati vakṣyāmi samāhitamanāvilam ॥ 29 ॥</p>
<p>dharmārthakāmamōkṣāṇāmidamēva parāyaṇam ।<br/>
hastyaśvarathapādāti sarvaiśvarya pradāyakam ॥ 30 ॥</p>
<p>putramitrakaḻatrādi sarvasantōṣasādhanam ।<br/>
vēdaśāstrādividyānāṃ vidhānaṃ paramaṃ hi tat ॥ 31 ॥</p>
<p>saṅgīta śāstra sāhitya satkavitva vidhāyakam ।<br/>
buddhi vidyā smṛti prajñā mati prauḍhipradāyakam ॥ 32 ॥</p>
<p>sarvasantōṣakaraṇaṃ sarvaduḥkhanivāraṇam ।<br/>
śatrusaṃhārakaṃ śīghraṃ yaśaḥkīrtivivardhanam ॥ 33 ॥</p>
<p>aṣṭasaṅkhyā mahārōgāḥ sannipātāstrayōdaśa ।<br/>
ṣaṇṇavatyakṣirōgāścha viṃśatirmēharōgakāḥ ॥ 34 ॥</p>
<p>aṣṭādaśatu kuṣṭhāni gulmānyaṣṭavidhānyapi ।<br/>
aśītirvātarōgāścha chatvāriṃśattu paittikāḥ ॥ 35 ॥</p>
<p>viṃśatiḥ ślēṣmarōgāścha kṣayachāturthikādayaḥ ।<br/>
mantrayantrakuyōgādyāḥ kalpatantrādinirmitāḥ ॥ 36 ॥</p>
<p>brahmarākṣasa vētālakūṣmāṇḍādi grahōdbhavāḥ ।<br/>
saṅgajā dēśakālasthāstāpatrayasamutthitāḥ ॥ 37 ॥</p>
<p>navagrahasamudbhūtā mahāpātaka sambhavāḥ ।<br/>
sarvē rōgāḥ praṇaśyanti sahasrāvartanād dhruvam ॥ 38 ॥</p>
<p>ayutāvṛttimātrēṇa vandhyā putravatī bhavēt ।<br/>
ayutadvitayāvṛttyā hyapamṛtyujayō bhavēt ॥ 39 ॥</p>
<p>ayutatritayāchchaiva khēcharatvaṃ prajāyatē ।<br/>
sahasrāyutadarvāk sarvakāryāṇi sādhayēt ॥ 40 ॥</p>
<p>lakṣāvṛttyā sarvasiddhirbhavatyēva na saṃśayaḥ ॥ 41 ॥</p>
<p>viṣavṛkṣasya mūlēṣu tiṣṭhan vai dakṣiṇāmukhaḥ ।<br/>
kurutē māsamātrēṇa vairiṇaṃ vikalēndriyam ॥ 42 ॥</p>
<p>audumbaratarōrmūlē vṛddhikāmēna jāpyatē ।<br/>
śrīvṛkṣamūlē śrīkāmī tintriṇī śāntikarmaṇi ॥ 43 ॥</p>
<p>ōjaskāmō'śvatthamūlē strīkāmaiḥ sahakārakē ।<br/>
jñānārthī tulasīmūlē garbhagēhē sutārthibhiḥ ॥ 44 ॥</p>
<p>dhanārthibhistu sukṣētrē paśukāmaistu gōṣṭhakē ।<br/>
dēvālayē sarvakāmaistatkālē sarvadarśitam ॥ 45 ॥</p>
<p>nābhimātrajalē sthitvā bhānumālōkya yō japēt ।<br/>
yuddhē vā śāstravādē vā sahasrēṇa jayō bhavēt ॥ 46 ॥</p>
<p>kaṇṭhamātrē jalē sthitvā yō rātrau kavachaṃ paṭhēt ।<br/>
jvarāpasmārakuṣṭhādi tāpajvaranivāraṇam ॥ 47 ॥</p>
<p>yatra yat syāt sthiraṃ yadyat prasaktaṃ tannivartatē ।<br/>
tēna tatra hi japtavyaṃ tataḥ siddhirbhavēddhruvam ॥ 48 ॥</p>
<p>ityuktavān śivō gaurvai rahasyaṃ paramaṃ śubham ।<br/>
yaḥ paṭhēt vajrakavachaṃ dattātrēya samō bhavēt ॥ 49 ॥</p>
<p>ēvaṃ śivēna kathitaṃ himavatsutāyai<br/>
