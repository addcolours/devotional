<p>phalaśrutirnāma dvādaśō'dhyāyaḥ ॥</p>
<p><b>dhyānaṃ</b><br>
vidhyuddhāma samaprabhāṃ mṛgapati skandha sthitāṃ bhīṣaṇāṃ।<br>
kanyābhiḥ karavāla khēṭa vilasaddastābhi rāsēvitāṃ<br>
hastaiśchakra gadhāsi khēṭa viśikhāṃ guṇaṃ tarjanīṃ<br>
vibhrāṇa manalātmikāṃ śiśidharāṃ durgāṃ trinētrāṃ bhajē</p>
<p>dēvyuvācha॥1॥</p>
<p>ēbhiḥ stavaiścha mā nityaṃ stōṣyatē yaḥ samāhitaḥ।<br>
tasyāhaṃ sakalāṃ bādhāṃ nāśayiṣyāmya saṃśayam           ॥2॥</p>
<p>madhukaiṭabhanāśaṃ cha mahiṣāsuraghātanam।<br>
kīrtiyiṣyanti yē ta dvadvadhaṃ śumbhaniśumbhayōḥ   ॥3॥           </p>
<p>aṣṭamyāṃ cha chaturdhaśyāṃ navamyāṃ chaikachētasaḥ।<br>
śrōṣyanti chaiva yē bhaktyā mama māhātmyamuttamam   ॥4॥           </p>
<p>na tēṣāṃ duṣkṛtaṃ kiñchid duṣkṛtōtthā na chāpadaḥ।<br>
bhaviṣyati na dāridryaṃ na chai vēṣṭaviyōjanam   ॥5॥           </p>
<p>śatrubhyō na bhayaṃ tasya dasyutō vā na rājataḥ।<br>
na śastrānalatō yaughāt kadāchit sambhaviṣyati   ॥6॥           </p>
<p>tasmānmamaitanmāhatmyaṃ paṭhitavyaṃ samāhitaiḥ।<br>
śrōtavyaṃ cha sadā bhaktyā paraṃ svastyayanaṃ hi tat   ॥7॥           </p>
<p>upa sargāna śēṣāṃstu mahāmārī samudbhavān।<br>
tathā trividha mutpātaṃ māhātmyaṃ śamayēnmama   ॥8॥   <br>
        <br>
yatraita tpaṭhyatē samyaṅnityamāyatanē mama।<br>
sadā na tadvimōkṣyāmi sānnidhyaṃ tatra mēsthitam   ॥9॥           </p>
<p>bali pradānē pūjāyāmagni kāryē mahōtsavē।<br>
sarvaṃ mamaitanmāhātmyaṃ uchchāryaṃ śrāvyamēvacha   ॥10॥           </p>
<p>jānatājānatā vāpi bali pūjāṃ tathā kṛtām।<br>
pratīkṣiṣyāmyahaṃ prītyā vahni hōmaṃ tathā kṛtam   ॥11॥           </p>
<p>śaratkālē mahāpūjā kriyatē yācha vārṣikī।<br>
tasyāṃ mamaitanmāhātmyaṃ śrutvā bhaktisamanvitaḥ   ॥12॥</p>
<p>sarvabādhāvinirmuktō dhanadhānyasamanvitaḥ।<br>
manuṣyō matprasādēna bhaviṣyati na saṃśayaḥ॥13॥</p>
<p>śrutvā mamaitanmāhātmyaṃ tathā chōtpattayaḥ śubhāḥ।<br>
parākramaṃ cha yuddhēṣu jāyatē nirbhayaḥ pumān॥14॥</p>
<p>ripavaḥ saṅkṣayaṃ yānti kaḻyāṇāṃ chōpapadhyatē।<br>
nandatē cha kulaṃ puṃsāṃ mahātmyaṃ mamaśṛṇvatām॥15॥</p>
<p>śāntikarmāṇi sarvatra tathā duḥsvapnadarśanē।<br>
grahapīḍāsu chōgrāsu mahātmyaṃ śṛṇuyānmama॥16॥</p>
<p>upasargāḥ śamaṃ yānti grahapīḍāścha dāruṇāḥ<br>
duḥsvapnaṃ cha nṛbhirdṛṣṭaṃ susvapnamupajāyatē॥17॥</p>
<p>bālagrahābhibhūtānaṃ bālānāṃ śāntikārakam।<br>
saṅghātabhēdē cha nṛṇāṃ maitrīkaraṇamuttamam॥18॥</p>
<p>durvṛttānāmaśēṣāṇāṃ balahānikaraṃ param।<br>
rakṣōbhūtapiśāchānāṃ paṭhanādēva nāśanam॥19॥</p>
<p>sarvaṃ mamaitanmāhātmyaṃ mama sannidhikārakam।<br>
paśupuṣpārghyadhūpaiścha gandhadīpaistathōttamaiḥ॥20॥</p>
<p>viprāṇāṃ bhōjanairhōmaiḥ prokṣaṇīyairaharniśam।<br>
anyaiścha vividhairbhōgaiḥ pradānairvatsarēṇa yā॥21॥</p>
<p>prītirmē kriyatē sāsmin sakṛduchcharitē śrutē।<br>
śrutaṃ harati pāpāni tathārōgyaṃ prayachChati॥22॥</p>
<p>rakṣāṃ karōti bhūtēbhyō janmanāṃ kīrtinaṃ mama।<br>
yuddēṣu charitaṃ yanmē duṣṭa daitya nibarhaṇam॥23॥</p>
<p>tasmiñChṛtē vairikṛtaṃ bhayaṃ puṃsāṃ na jāyatē।<br>
yuṣmābhiḥ stutayō yāścha yāścha brahmarṣibhiḥ kṛtāḥ॥24॥</p>
<p>brahmaṇā cha kṛtāstāstu prayachChantu śubhāṃ matim।<br>
araṇyē prāntarē vāpi dāvāgni parivāritaḥ॥25॥</p>
<p>dasyubhirvā vṛtaḥ śūnyē gṛhītō vāpi śatṛbhiḥ।<br>
siṃhavyāghrānuyātō vā vanēvā vana hastibhiḥ॥26॥</p>
<p>rājñā kruddēna chājñaptō vadhyō banda gatō'pivā।<br>
āghūrṇitō vā vātēna sthitaḥ pōtē mahārṇavē॥27॥</p>
<p>patatsu chāpi śastrēṣu saṅgrāmē bhṛśadāruṇē।<br>
sarvābādhāśu ghōrāsu vēdanābhyarditō'pivā॥28॥</p>
<p>smaran mamaitachcharitaṃ narō muchyēta saṅkaṭāt।<br>
mama prabhāvātsiṃhādyā dasyavō vairiṇa stathā॥29॥</p>
<p>dūrādēva palāyantē smarataścharitaṃ mama॥30॥</p>
<p>ṛṣiruvācha॥31॥</p>
<p>ityuktvā sā bhagavatī chaṇḍikā chaṇḍavikramā।<br>
paśyatāṃ sarva dēvānāṃ tatraivāntaradhīyata॥32॥</p>
<p>tē'pi dēvā nirātaṅkāḥ svādhikārānyathā purā।<br>
yajñabhāgabhujaḥ sarvē chakrurvi nihatārayaḥ॥33॥</p>
<p>daityāścha dēvyā nihatē śumbhē dēvarip'u yudhi <br>
jagadvidhvaṃsakē tasmin mahōgrē'tula vikramē॥34॥</p>
<p>niśumbhē cha mahāvīryē śēṣāḥ pātāḻamāyayuḥ॥35॥</p>
<p>ēvaṃ bhagavatī dēvī sā nityāpi punaḥ punaḥ।<br>
sambhūya kurutē bhūpa jagataḥ paripālanam॥36॥</p>
<p>tayaitanmōhyatē viśvaṃ saiva viśvaṃ prasūyatē।<br>
sāyāchitā cha vijñānaṃ tuṣṭā ṛddhiṃ prayachChati॥37॥</p>
<p>vyāptaṃ tayaitatsakalaṃ brahmāṇḍaṃ manujēśvara।<br>
mahādēvyā mahākāḻī mahāmārī svarūpayā॥38॥</p>
<p>saiva kālē mahāmārī saiva sṛṣtirbhavatyajā।<br>
sthitiṃ karōti bhūtānāṃ saiva kālē sanātanī॥39॥</p>
<p>bhavakālē nṛṇāṃ saiva lakṣmīrvṛddhipradā gṛhē।<br>
saivābhāvē tathā lakṣmī rvināśāyōpajāyatē॥40॥</p>
<p>stutā sampūjitā puṣpairgandhadhūpādibhistathā।<br>
dadāti vittaṃ putrāṃścha matiṃ dharmē gatiṃ śubhāṃ॥41॥</p>
<p>॥ iti śrī mārkaṇḍēya purāṇē sāvarnikē manvantarē dēvī mahatmyē phalaśrutirnāma dvādaśō'dhyāya samāptam ॥</p>
