rōgādvai muchyatē rōgī baddhō muchyēta bandhanāt ॥ 162 ॥</p>
<p>brahmahatyā surāpānaṃ suvarṇastēyinō narāḥ ।<br/>
gurutalpagatō vāpi pātakānmuchyatē sakṛt ॥ 163 ॥</p>
<p>asatpratigrahāchchaivā'bhakṣyabhakṣādviśēṣataḥ ।<br/>
pākhaṇḍānṛtamukhyēbhyaḥ paṭhanādēva muchyatē ॥ 164 ॥</p>
