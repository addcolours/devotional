aṣṭamyāṃ cha viśēṣēṇa paṭhitavyaṃ dvijaiḥ saha ॥ 157 ॥</p>
<p>japaṃ kṛtvā hōma pūjā dhyānaṃ kṛtvā viśēṣataḥ ।<br/>
yasmai kasmai na dātavyaṃ gāyatryāstu viśēṣataḥ ॥ 158 ॥</p>
<p>subhaktāya suśiṣyāya vaktavyaṃ bhūsurāya vai ।<br/>
bhraṣṭēbhyaḥ sādhakēbhyaścha bāndhavēbhyō na darśayēt ॥ 159 ॥</p>
