               <div class="stotramtext" id="stext">
                  <span>
                     <span style="font-family:NotoSans; line-height:150%;">
                        <span style="font-family:NotoSans; line-height:150%;">
<p>nārada uvācha –<br/>
bhagavansarvadharmajña sarvaśāstraviśārada ।<br/>
śrutismṛtipurāṇānāṃ rahasyaṃ tvanmukhāchChrutam ॥ 1 ॥</p>
<p>sarvapāpaharaṃ dēva yēna vidyā pravartatē ।<br/>
kēna vā brahmavijñānaṃ kiṃ nu vā mōkṣasādhanam ॥ 2 ॥</p>
<p>brāhmaṇānāṃ gatiḥ kēna kēna vā mṛtyu nāśanam ।<br/>
aihikāmuṣmikaphalaṃ kēna vā padmalōchana ॥ 3 ॥</p>
<p>vaktumarhasyaśēṣēṇa sarvē nikhilamāditaḥ ।<br/>
śrīnārāyaṇa uvācha –<br/>
sādhu sādhu mahāprājña samyak pṛṣṭaṃ tvayā'nagha ॥ 4 ॥</p>
<p>śṛṇu vakṣyāmi yatnēna gāyatryaṣṭasahasrakam ।<br/>
nāmnāṃ śubhānāṃ divyānāṃ sarvapāpavināśanam ॥ 5 ॥</p>
<p>sṛṣṭyādau yadbhagavatā pūrvē prōktaṃ bravīmi tē ।<br/>
aṣṭōttarasahasrasya ṛṣirbrahmā prakīrtitaḥ ॥ 6 ॥</p>
<p>Chandō'nuṣṭuptathā dēvī gāyatrīṃ dēvatā smṛtā ।<br/>
halōbījāni tasyaiva svarāḥ śaktaya īritāḥ ॥ 7 ॥</p>
<p>aṅganyāsakaranyāsāvuchyētē mātṛkākṣaraiḥ ।<br/>
atha dhyānaṃ pravakṣyāmi sādhakānāṃ hitāya vai ॥ 8 ॥</p>
<p><b>dhyānam</b><br/>
raktaśvētahiraṇyanīladhavalairyuktāṃ trinītrōjjvalāṃ<br/>
raktāṃ raktanavasrajaṃ maṇigaṇairyuktāṃ kumārīmimām ।<br/>
gāyatrīṃ kamalāsanāṃ karatalavyānaddhakuṇḍāmbujāṃ<br/>
padmākṣīṃ cha varasrajaṃ cha dadhatīṃ haṃsādhirūḍhāṃ bhajē ॥ 9 ॥</p>
<p>achintyalakṣaṇāvyaktāpyarthamātṛmahēśvarī ।<br/>
amṛtārṇavamadhyasthāpyajitā chāparājitā ॥ 10 ॥</p>
<p>aṇimādiguṇādhārāpyarkamaṇḍalasaṃsthitā ।<br/>
ajarājāparādharmā akṣasūtradharādharā ॥ 11 ॥</p>
<p>akārādikṣakārāntāpyariṣaḍvargabhēdinī ।<br/>
añjanādripratīkāśāpyañjanādrinivāsinī ॥ 12 ॥</p>
<p>aditiśchājapāvidyāpyaravindanibhēkṣaṇā ।<br/>
antarbahiḥsthitāvidyādhvaṃsinī chāntarātmikā ॥ 13 ॥</p>
<p>ajā chājamukhāvāsāpyaravindanibhānanā ।<br/>
ardhamātrārthadānajñāpyarimaṇḍalamardinī ॥ 14 ॥</p>
<p>asuraghnī hyamāvāsyāpyalakṣmīghnyantyajārchitā ।<br/>
ādilakṣmīśchādiśaktirākṛtiśchāyatānanā ॥ 15 ॥</p>
<p>ādityapadavīchārāpyādityaparisēvitā ।<br/>
āchāryāvartanāchārāpyādimūrtinivāsinī ॥ 16 ॥</p>
<p>āgnēyī chāmarī chādyā chārādhyā chāsanasthitā ।<br/>
ādhāranilayādhārā chākāśāntanivāsinī ॥ 17 ॥</p>
<p>ādyākṣarasamāyuktā chāntarākāśarūpiṇī ।<br/>
ādityamaṇḍalagatā chāntaradhvāntanāśinī ॥ 18 ॥</p>
<p>indirā chēṣṭadā chēṣṭā chēndīvaranibhēkṣaṇā ।<br/>
irāvatī chēndrapadā chēndrāṇī chēndurūpiṇī ॥ 19 ॥</p>
<p>ikṣukōdaṇḍasaṃyuktā chēṣusandhānakāriṇī ।<br/>
indranīlasamākārā chēḍāpiṅgalarūpiṇī ॥ 20 ॥</p>
<p>indrākṣīchēśvarī dēvī chēhātrayavivarjitā ।<br/>
umā chōṣā hyuḍunibhā urvārukaphalānanā ॥ 21 ॥</p>
<p>uḍuprabhā chōḍumatī hyuḍupā hyuḍumadhyagā ।<br/>
ūrdhvā chāpyūrdhvakēśī chāpyūrdhvādhōgatibhēdinī ॥ 22 ॥</p>
<p>ūrdhvabāhupriyā chōrmimālāvāggranthadāyinī ।<br/>
ṛtaṃ charṣirṛtumatī ṛṣidēvanamaskṛtā ॥ 23 ॥</p>
<p>ṛgvēdā ṛṇahartrī cha ṛṣimaṇḍalachāriṇī ।<br/>
ṛddhidā ṛjumārgasthā ṛjudharmā ṛjupradā ॥ 24 ॥</p>
<p>ṛgvēdanilayā ṛjvī luptadharmapravartinī ।<br/>
lūtārivarasambhūtā lūtādiviṣahāriṇī ॥ 25 ॥</p>
<p>ēkākṣarā chaikamātrā chaikā chaikaikaniṣṭhitā ।<br/>
aindrī hyairāvatārūḍhā chaihikāmuṣmikapradā ॥ 26 ॥</p>
<p>ōṅkārā hyōṣadhī chōtā chōtaprōtanivāsinī ।<br/>
aurvā hyauṣadhasampannā aupāsanaphalapradā ॥ 27 ॥</p>
<p>aṇḍamadhyasthitā dēvī chāḥkāramanurūpiṇī ।<br/>
kātyāyanī kālarātriḥ kāmākṣī kāmasundarī ॥ 28 ॥</p>
<p>kamalā kāminī kāntā kāmadā kālakaṇṭhinī ।<br/>
karikumbhastanabharā karavīrasuvāsinī ॥ 29 ॥</p>
<p>kalyāṇī kuṇḍalavatī kurukṣētranivāsinī ।<br/>
kuruvindadalākārā kuṇḍalī kumudālayā ॥ 30 ॥</p>
<p>kālajihvā karālāsyā kālikā kālarūpiṇī ।<br/>
kamanīyaguṇā kāntiḥ kalādhārā kumudvatī ॥ 31 ॥</p>
<p>kauśikī kamalākārā kāmachāraprabhañjinī ।<br/>
kaumārī karuṇāpāṅgī kakuvantā karipriyā ॥ 32 ॥</p>
<p>kēsarī kēśavanutā kadambakusumapriyā ।<br/>
kālindī kālikā kāñchī kalaśōdbhavasaṃstutā ॥ 33 ॥</p>
<p>kāmamātā kratumatī kāmarūpā kṛpāvatī ।<br/>
kumārī kuṇḍanilayā kirātī kīravāhanā ॥ 34 ॥</p>
<p>kaikēyī kōkilālāpā kētakī kusumapriyā ।<br/>
kamaṇḍaludharā kālī karmanirmūlakāriṇī ॥ 35 ॥</p>
<p>kalahaṃsagatiḥ kakṣā kṛtakautukamaṅgalā ।<br/>
kastūrītilakā kamprā karīndragamanā kuhūḥ ॥ 36 ॥</p>
<p>karpūralēpanā kṛṣṇā kapilā kuharāśrayā ।<br/>
kūṭasthā kudharā kamrā kukṣisthākhilaviṣṭapā ॥ 37 ॥</p>
<p>khaḍgakhēṭadharā kharvā khēcharī khagavāhanā ।<br/>
khaṭvāṅgadhāriṇī khyātā khagarājōparisthitā ॥ 38 ॥</p>
<p>khalaghnī khaṇḍitajarā khaṇḍākhyānapradāyinī ।<br/>
khaṇḍēndutilakā gaṅgā gaṇēśaguhapūjitā ॥ 39 ॥</p>
<p>gāyatrī gōmatī gītā gāndhārī gānalōlupā ।<br/>
gautamī gāminī gādhā gandharvāpsarasēvitā ॥ 40 ॥</p>
<p>gōvindacharaṇākrāntā guṇatrayavibhāvitā ।<br/>
gandharvī gahvarī gōtrā girīśā gahanā gamī ॥ 41 ॥</p>
<p>guhāvāsā guṇavatī gurupāpapraṇāśinī ।<br/>
gurvī guṇavatī guhyā gōptavyā guṇadāyinī ॥ 42 ॥</p>
<p>girijā guhyamātaṅgī garuḍadhvajavallabhā ।<br/>
garvāpahāriṇī gōdā gōkulasthā gadādharā ॥ 43 ॥</p>
<p>gōkarṇanilayāsaktā guhyamaṇḍalavartinī ।<br/>
gharmadā ghanadā ghaṇṭā ghōradānavamardinī ॥ 44 ॥</p>
<p>ghṛṇimantramayī ghōṣā ghanasampātadāyinī ।<br/>
ghaṇṭāravapriyā ghrāṇā ghṛṇisantuṣṭakāriṇī ॥ 45 ॥</p>
<p>ghanārimaṇḍalā ghūrṇā ghṛtāchī ghanavēginī ।<br/>
jñānadhātumayī charchā charchitā chāruhāsinī ॥ 46 ॥</p>
<p>chaṭulā chaṇḍikā chitrā chitramālyavibhūṣitā ।<br/>
chaturbhujā chārudantā chāturī charitapradā ॥ 47 ॥</p>
<p>chūlikā chitravastrāntā chandramaḥkarṇakuṇḍalā ।<br/>
chandrahāsā chārudātrī chakōrī chandrahāsinī ॥ 48 ॥</p>
<p>chandrikā chandradhātrī cha chaurī chaurā cha chaṇḍikā ।<br/>
chañchadvāgvādinī chandrachūḍā chōravināśinī ॥ 49 ॥</p>
<p>chāruchandanaliptāṅgī chañchachchāmaravījitā ।<br/>
chārumadhyā chārugatiśchandilā chandrarūpiṇī ॥ 50 ॥</p>
<p>chāruhōmapriyā chārvācharitā chakrabāhukā ।<br/>
chandramaṇḍalamadhyasthā chandramaṇḍaladarpaṇā ॥ 51 ॥</p>
<p>chakravākastanī chēṣṭā chitrā chāruvilāsinī ।<br/>
chitsvarūpā chandravatī chandramāśchandanapriyā ॥ 52 ॥</p>
<p>chōdayitrī chiraprajñā chātakā chāruhētukī ।<br/>
Chatrayātā Chatradharā Chāyā ChandaḥparichChadā ॥ 53 ॥</p>
<p>Chāyādēvī Chidranakhā Channēndriyavisarpiṇī ।<br/>
Chandō'nuṣṭuppratiṣṭhāntā Chidrōpadravabhēdinī ॥ 54 ॥</p>
<p>Chēdā Chatrēśvarī Chinnā Churikā Chēdanapriyā ।<br/>
jananī janmarahitā jātavēdā jaganmayī ॥ 55 ॥</p>
<p>jāhnavī jaṭilā jētrī jarāmaraṇavarjitā ।<br/>
jambūdvīpavatī jvālā jayantī jalaśālinī ॥ 56 ॥</p>
<p>jitēndriyā jitakrōdhā jitāmitrā jagatpriyā ।<br/>
jātarūpamayī jihvā jānakī jagatī jarā ॥ 57 ॥</p>
<p>janitrī jahnutanayā jagattrayahitaiṣiṇī ।<br/>
jvālāmukhī japavatī jvaraghnī jitaviṣṭapā ॥ 58 ॥</p>
<p>jitākrāntamayī jvālā jāgratī jvaradēvatā ।<br/>
jvalantī jaladā jyēṣṭhā jyāghōṣāsphōṭadiṅmukhī ॥ 59 ॥</p>
<p>jambhinī jṛmbhaṇā jṛmbhā jvalanmāṇikyakuṇḍalā ।<br/>
jhiñjhikā jhaṇanirghōṣā jhañjhāmārutavēginī ॥ 60 ॥</p>
<p>jhallarīvādyakuśalā ñarūpā ñabhujā smṛtā ।<br/>
ṭaṅkabāṇasamāyuktā ṭaṅkinī ṭaṅkabhēdinī ॥ 61 ॥</p>
<p>ṭaṅkīgaṇakṛtāghōṣā ṭaṅkanīyamahōrasā ।<br/>
ṭaṅkārakāriṇī dēvī ṭhaṭhaśabdaninādinī ॥ 62 ॥</p>
<p>ḍāmarī ḍākinī ḍimbhā ḍuṇḍumāraikanirjitā ।<br/>
ḍāmarītantramārgasthā ḍamaḍḍamarunādinī ॥ 63 ॥</p>
<p>ḍiṇḍīravasahā ḍimbhalasatkrīḍāparāyaṇā ।<br/>
ḍhuṇḍhivighnēśajananī ḍhakkāhastā ḍhilivrajā ॥ 64 ॥</p>
<p>nityajñānā nirupamā nirguṇā narmadā nadī ।<br/>
triguṇā tripadā tantrī tulasī taruṇā taruḥ ॥ 65 ॥</p>
<p>trivikramapadākrāntā turīyapadagāminī ।<br/>
taruṇādityasaṅkāśā tāmasī tuhinā turā ॥ 66 ॥</p>
<p>trikālajñānasampannā trivēṇī cha trilōchanā ।<br/>
triśaktistripurā tuṅgā turaṅgavadanā tathā ॥ 67 ॥</p>
<p>timiṅgilagilā tīvrā trisrōtā tāmasādinī ।<br/>
tantramantraviśēṣajñā tanumadhyā triviṣṭapā ॥ 68 ॥</p>
<p>trisandhyā tristanī tōṣāsaṃsthā tālapratāpinī ।<br/>
tāṭaṅkinī tuṣārābhā tuhināchalavāsinī ॥ 69 ॥</p>
<p>tantujālasamāyuktā tārahārāvalipriyā ।<br/>
tilahōmapriyā tīrthā tamālakusumākṛtiḥ ॥ 70 ॥</p>
<p>tārakā triyutā tanvī triśaṅkuparivāritā ।<br/>
talōdarī tilābhūṣā tāṭaṅkapriyavādinī ॥ 71 ॥</p>
<p>trijaṭā tittirī tṛṣṇā trividhā taruṇākṛtiḥ ।<br/>
taptakāñchanasaṅkāśā taptakāñchanabhūṣaṇā ॥ 72 ॥</p>
<p>traiyambakā trivargā cha trikālajñānadāyinī ।<br/>
tarpaṇā tṛptidā tṛptā tāmasī tumburustutā ॥ 73 ॥</p>
<p>tārkṣyasthā triguṇākārā tribhaṅgī tanuvallariḥ ।<br/>
thātkārī thāravā thāntā dōhinī dīnavatsalā ॥ 74 ॥</p>
<p>dānavāntakarī durgā durgāsuranibarhiṇī ।<br/>
dēvarītirdivārātrirdraupadī dundubhisvanā ॥ 75 ॥</p>
<p>dēvayānī durāvāsā dāridryōdbhēdinī divā ।<br/>
dāmōdarapriyā dīptā digvāsā digvimōhinī ॥ 76 ॥</p>
<p>daṇḍakāraṇyanilayā daṇḍinī dēvapūjitā ।<br/>
dēvavandyā diviṣadā dvēṣiṇī dānavākṛtiḥ ॥ 77 ॥</p>
<p>dīnānāthastutā dīkṣā daivatādisvarūpiṇī ।<br/>
dhātrī dhanurdharā dhēnurdhāriṇī dharmachāriṇī ॥ 78 ॥</p>
<p>dhurandharā dharādhārā dhanadā dhānyadōhinī ।<br/>
dharmaśīlā dhanādhyakṣā dhanurvēdaviśāradā ॥ 79 ॥</p>
<p>dhṛtirdhanyā dhṛtapadā dharmarājapriyā dhruvā ।<br/>
dhūmāvatī dhūmakēśī dharmaśāstraprakāśinī ॥ 80 ॥</p>
<p>nandā nandapriyā nidrā nṛnutā nandanātmikā ।<br/>
narmadā nalinī nīlā nīlakaṇṭhasamāśrayā ॥ 81 ॥</p>
<p>nārāyaṇapriyā nityā nirmalā nirguṇā nidhiḥ ।<br/>
nirādhārā nirupamā nityaśuddhā nirañjanā ॥ 82 ॥</p>
<p>nādabindukalātītā nādabindukalātmikā ।<br/>
nṛsiṃhinī nagadharā nṛpanāgavibhūṣitā ॥ 83 ॥</p>
<p>narakaklēśaśamanī nārāyaṇapadōdbhavā ।<br/>
niravadyā nirākārā nāradapriyakāriṇī ॥ 84 ॥</p>
<p>nānājyōtiḥ samākhyātā nidhidā nirmalātmikā ।<br/>
navasūtradharā nītirnirupadravakāriṇī ॥ 85 ॥</p>
<p>nandajā navaratnāḍhyā naimiṣāraṇyavāsinī ।<br/>
navanītapriyā nārī nīlajīmūtanisvanā ॥ 86 ॥</p>
<p>nimēṣiṇī nadīrūpā nīlagrīvā niśīśvarī ।<br/>
nāmāvalirniśumbhaghnī nāgalōkanivāsinī ॥ 87 ॥</p>
<p>navajāmbūnadaprakhyā nāgalōkādhidēvatā ।<br/>
nūpurākrāntacharaṇā narachittapramōdinī ॥ 88 ॥</p>
<p>nimagnāraktanayanā nirghātasamanisvanā ।<br/>
nandanōdyānanilayā nirvyūhōparichāriṇī ॥ 89 ॥</p>
<p>pārvatī paramōdārā parabrahmātmikā parā ।<br/>
pañchakōśavinirmuktā pañchapātakanāśinī ॥ 90 ॥</p>
<p>parachittavidhānajñā pañchikā pañcharūpiṇī ।<br/>
pūrṇimā paramā prītiḥ paratējaḥ prakāśinī ॥ 91 ॥</p>
<p>purāṇī pauruṣī puṇyā puṇḍarīkanibhēkṣaṇā ।<br/>
pātālatalanirmagnā prītā prītivivardhinī ॥ 92 ॥</p>
<p>pāvanī pādasahitā pēśalā pavanāśinī ।<br/>
prajāpatiḥ pariśrāntā parvatastanamaṇḍalā ॥ 93 ॥</p>
<p>padmapriyā padmasaṃsthā padmākṣī padmasambhavā ।<br/>
padmapatrā padmapadā padminī priyabhāṣiṇī ॥ 94 ॥</p>
<p>paśupāśavinirmuktā purandhrī puravāsinī ।<br/>
puṣkalā puruṣā parvā pārijātasumapriyā ॥ 95 ॥</p>
<p>pativratā pavitrāṅgī puṣpahāsaparāyaṇā ।<br/>
prajñāvatīsutā pautrī putrapūjyā payasvinī ॥ 96 ॥</p>
<p>paṭṭipāśadharā paṅktiḥ pitṛlōkapradāyinī ।<br/>
purāṇī puṇyaśīlā cha praṇatārtivināśinī ॥ 97 ॥</p>
<p>pradyumnajananī puṣṭā pitāmahaparigrahā ।<br/>
puṇḍarīkapurāvāsā puṇḍarīkasamānanā ॥ 98 ॥</p>
<p>pṛthujaṅghā pṛthubhujā pṛthupādā pṛthūdarī ।<br/>
pravālaśōbhā piṅgākṣī pītavāsāḥ prachāpalā ॥ 99 ॥</p>
<p>prasavā puṣṭidā puṇyā pratiṣṭhā praṇavāgatiḥ ।<br/>
pañchavarṇā pañchavāṇī pañchikā pañjarasthitā ॥ 100 ॥</p>
<p>paramāyā parajyōtiḥ paraprītiḥ parāgatiḥ ।<br/>
parākāṣṭhā parēśānī pāvanī pāvakadyutiḥ ॥ 101 ॥</p>
<p>puṇyabhadrā parichChēdyā puṣpahāsā pṛthūdarī ।<br/>
pītāṅgī pītavasanā pītaśayyā piśāchinī ॥ 102 ॥</p>
<p>pītakriyā piśāchaghnī pāṭalākṣī paṭukriyā ।<br/>
pañchabhakṣapriyāchārā pūtanāprāṇaghātinī ॥ 103 ॥</p>
<p>punnāgavanamadhyasthā puṇyatīrthaniṣēvitā ।<br/>
pañchāṅgī cha parāśaktiḥ paramāhlādakāriṇī ॥ 104 ॥</p>
<p>puṣpakāṇḍasthitā pūṣā pōṣitākhilaviṣṭapā ।<br/>
prāṇapriyā pañchaśikhā pannagōpariśāyinī ॥ 105 ॥</p>
<p>pañchamātrātmikā pṛthvī pathikā pṛthudōhinī ।<br/>
purāṇanyāyamīmāṃsā pāṭalī puṣpagandhinī ॥ 106 ॥</p>
<p>puṇyaprajā pāradātrī paramārgaikagōcharā ।<br/>
pravālaśōbhā pūrṇāśā praṇavā pallavōdarī ॥ 107 ॥</p>
<p>phalinī phaladā phalguḥ phūtkārī phalakākṛtiḥ ।<br/>
phaṇīndrabhōgaśayanā phaṇimaṇḍalamaṇḍitā ॥ 108 ॥</p>
<p>bālabālā bahumatā bālātapanibhāṃśukā ।<br/>
balabhadrapriyā vandyā baḍavā buddhisaṃstutā ॥ 109 ॥</p>
<p>bandīdēvī bilavatī baḍiśaghnī balipriyā ।<br/>
bāndhavī bōdhitā buddhirbandhūkakusumapriyā ॥ 110 ॥</p>
<p>bālabhānuprabhākārā brāhmī brāhmaṇadēvatā ।<br/>
bṛhaspatistutā bṛndā bṛndāvanavihāriṇī ॥ 111 ॥</p>
<p>bālākinī bilāhārā bilavāsā bahūdakā ।<br/>
bahunētrā bahupadā bahukarṇāvataṃsikā ॥ 112 ॥</p>
<p>bahubāhuyutā bījarūpiṇī bahurūpiṇī ।<br/>
bindunādakalātītā bindunādasvarūpiṇī ॥ 113 ॥</p>
<p>baddhagōdhāṅgulitrāṇā badaryāśramavāsinī ।<br/>
bṛndārakā bṛhatskandhā bṛhatī bāṇapātinī ॥ 114 ॥</p>
<p>bṛndādhyakṣā bahunutā vanitā bahuvikramā ।<br/>
baddhapadmāsanāsīnā bilvapatratalasthitā ॥ 115 ॥</p>
<p>bōdhidrumanijāvāsā baḍisthā bindudarpaṇā ।<br/>
bālā bāṇāsanavatī baḍabānalavēginī ॥ 116 ॥</p>
<p>brahmāṇḍabahirantaḥsthā brahmakaṅkaṇasūtriṇī ।<br/>
bhavānī bhīṣaṇavatī bhāvinī bhayahāriṇī ॥ 117 ॥</p>
<p>bhadrakālī bhujaṅgākṣī bhāratī bhāratāśayā ।<br/>
bhairavī bhīṣaṇākārā bhūtidā bhūtimālinī ॥ 118 ॥</p>
<p>bhāminī bhōganiratā bhadradā bhūrivikramā ।<br/>
bhūtavāsā bhṛgulatā bhārgavī bhūsurārchitā ॥ 119 ॥</p>
<p>bhāgīrathī bhōgavatī bhavanasthā bhiṣagvarā ।<br/>
bhāminī bhōginī bhāṣā bhavānī bhūridakṣiṇā ॥ 120 ॥</p>
<p>bhargātmikā bhīmavatī bhavabandhavimōchinī ।<br/>
bhajanīyā bhūtadhātrīrañjitā bhuvanēśvarī ॥ 121 ॥</p>
<p>bhujaṅgavalayā bhīmā bhēruṇḍā bhāgadhēyinī ।<br/>
mātā māyā madhumatī madhujihvā madhupriyā ॥ 122 ॥</p>
<p>mahādēvī mahābhāgā mālinī mīnalōchanā ।<br/>
māyātītā madhumatī madhumāṃsā madhudravā ॥ 123 ॥</p>
<p>mānavī madhusambhūtā mithilāpuravāsinī ।<br/>
madhukaiṭabhasaṃhartrī mēdinī mēghamālinī ॥ 124 ॥</p>
<p>mandōdarī mahāmāyā maithilī masṛṇapriyā ।<br/>
mahālakṣmīrmahākālī mahākanyā mahēśvarī ॥ 125 ॥</p>
<p>māhēndrī mērutanayā mandārakusumārchitā ।<br/>
mañjumañjīracharaṇā mōkṣadā mañjubhāṣiṇī ॥ 126 ॥</p>
<p>madhuradrāviṇī mudrā malayā malayānvitā ।<br/>
mēdhā marakataśyāmā māgadhī mēnakātmajā ॥ 127 ॥</p>
<p>mahāmārī mahāvīrā mahāśyāmā manustutā ।<br/>
mātṛkā mihirābhāsā mukundapadavikramā ॥ 128 ॥</p>
<p>mūlādhārasthitā mugdhā maṇipūrakavāsinī ।<br/>
mṛgākṣī mahiṣārūḍhā mahiṣāsuramardinī ॥ 129 ॥</p>
<p>yōgāsanā yōgagamyā yōgā yauvanakāśrayā ।<br/>
yauvanī yuddhamadhyasthā yamunā yugadhāriṇī ॥ 130 ॥</p>
<p>yakṣiṇī yōgayuktā cha yakṣarājaprasūtinī ।<br/>
yātrā yānavidhānajñā yaduvaṃśasamudbhavā ॥ 131 ॥</p>
<p>yakārādihakārāntā yājuṣī yajñarūpiṇī ।<br/>
yāminī yōganiratā yātudhānabhayaṅkarī ॥ 132 ॥</p>
<p>rukmiṇī ramaṇī rāmā rēvatī rēṇukā ratiḥ ।<br/>
raudrī raudrapriyākārā rāmamātā ratipriyā ॥ 133 ॥</p>
<p>rōhiṇī rājyadā rēvā ramā rājīvalōchanā ।<br/>
rākēśī rūpasampannā ratnasiṃhāsanasthitā ॥ 134 ॥</p>
<p>raktamālyāmbaradharā raktagandhānulēpanā ।<br/>
rājahaṃsasamārūḍhā rambhā raktabalipriyā ॥ 135 ॥</p>
<p>ramaṇīyayugādhārā rājitākhilabhūtalā ।<br/>
rurucharmaparīdhānā rathinī ratnamālikā ॥ 136 ॥</p>
<p>rōgēśī rōgaśamanī rāviṇī rōmaharṣiṇī ।<br/>
rāmachandrapadākrāntā rāvaṇachChēdakāriṇī ॥ 137 ॥</p>
<p>ratnavastraparichChannā rathasthā rukmabhūṣaṇā ।<br/>
lajjādhidēvatā lōlā lalitā liṅgadhāriṇī ॥ 138 ॥</p>
<p>lakṣmīrlōlā luptaviṣā lōkinī lōkaviśrutā ।<br/>
lajjā lambōdarī dēvī lalanā lōkadhāriṇī ॥ 139 ॥</p>
<p>varadā vanditā vidyā vaiṣṇavī vimalākṛtiḥ ।<br/>
vārāhī virajā varṣā varalakṣmīrvilāsinī ॥ 140 ॥</p>
<p>vinatā vyōmamadhyasthā vārijāsanasaṃsthitā ।<br/>
vāruṇī vēṇusambhūtā vītihōtrā virūpiṇī ॥ 141 ॥</p>
<p>vāyumaṇḍalamadhyasthā viṣṇurūpā vidhipriyā ।<br/>
viṣṇupatnī viṣṇumatī viśālākṣī vasundharā ॥ 142 ॥</p>
<p>vāmadēvapriyā vēlā vajriṇī vasudōhinī ।<br/>
vēdākṣaraparītāṅgī vājapēyaphalapradā ॥ 143 ॥</p>
<p>vāsavī vāmajananī vaikuṇṭhanilayā varā ।<br/>
vyāsapriyā varmadharā vālmīkiparisēvitā ॥ 144 ॥</p>
<p>śākambharī śivā śāntā śāradā śaraṇāgatiḥ ।<br/>
śātōdarī śubhāchārā śumbhāsuravimardinī ॥ 145 ॥</p>
<p>śōbhāvatī śivākārā śaṅkarārdhaśarīriṇī ।<br/>
śōṇā śubhāśayā śubhrā śiraḥsandhānakāriṇī ॥ 146 ॥</p>
<p>śarāvatī śarānandā śarajjyōtsnā śubhānanā ।<br/>
śarabhā śūlinī śuddhā śabarī śukavāhanā ॥ 147 ॥</p>
<p>śrīmatī śrīdharānandā śravaṇānandadāyinī ।<br/>
śarvāṇī śarvarīvandyā ṣaḍbhāṣā ṣaḍṛtupriyā ॥ 148 ॥</p>
<p>ṣaḍādhārasthitā dēvī ṣaṇmukhapriyakāriṇī ।<br/>
ṣaḍaṅgarūpasumatī surāsuranamaskṛtā ॥ 149 ॥</p>
<p>sarasvatī sadādhārā sarvamaṅgalakāriṇī ।<br/>
sāmagānapriyā sūkṣmā sāvitrī sāmasambhavā ॥ 150 ॥</p>
<p>sarvāvāsā sadānandā sustanī sāgarāmbarā ।<br/>
sarvaiśvaryapriyā siddhiḥ sādhubandhuparākramā ॥ 151 ॥</p>
<p>saptarṣimaṇḍalagatā sōmamaṇḍalavāsinī ।<br/>
sarvajñā sāndrakaruṇā samānādhikavarjitā ॥ 152 ॥</p>
<p>sarvōttuṅgā saṅgahīnā sadguṇā sakalēṣṭadā ।<br/>
saradhā sūryatanayā sukēśī sōmasaṃhatiḥ ॥ 153 ॥</p>
<p>hiraṇyavarṇā hariṇī hrīṅkārī haṃsavāhinī ।<br/>
kṣaumavastraparītāṅgī kṣīrābdhitanayā kṣamā ॥ 154 ॥</p>
<p>gāyatrī chaiva sāvitrī pārvatī cha sarasvatī ।<br/>
vēdagarbhā varārōhā śrīgāyatrī parāmbikā ॥ 155 ॥</p>
