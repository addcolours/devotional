chañchalāpisthirā bhūtvā kamalā tatra tiṣṭhati ॥ 160 ॥</p>
<p>idaṃ rahasyaṃ paramaṃ guhyādguhyataraṃ mahat ।<br/>
puṇyapradaṃ manuṣyāṇāṃ daridrāṇāṃ nidhipradam ॥ 161 ॥</p>
