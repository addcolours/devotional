yanmukhē dhriyatē nityaṃ durgānāmasahasrakam ॥ 7॥ </p>
<p>kiṃ tasyētaramantraughaiḥ kāryaṃ dhanyatamasya hi । <br>
durgānāmasahasrasya pustakaṃ yadgṛhē bhavēt ॥ 8॥ </p>
<p>na tatra grahabhūtādibādhā syānmaṅgalāspadē । <br>
tadgṛhaṃ puṇyadaṃ kṣētraṃ dēvīsānnidhyakārakam ॥ 9॥</p>
<p>ētasya stōtramukhyasya pāṭhakaḥ śrēṣṭhamantravit । <br>
dēvatāyāḥ prasādēna sarvapūjyaḥ sukhī bhavēt ॥ 10॥ </p>
