<p>॥ atha śrī durgā sahasranāmastōtram ॥</p>
<p>nārada uvācha - <br>
kumāra guṇagambhīra dēvasēnāpatē prabhō । <br>
sarvābhīṣṭapradaṃ puṃsāṃ sarvapāpapraṇāśanam ॥ 1॥ </p>
<p>guhyādguhyataraṃ stōtraṃ bhaktivardhakamañjasā । <br>
maṅgalaṃ grahapīḍādiśāntidaṃ vaktumarhasi ॥ 2॥ </p>
<p>skanda uvācha - <br>
śṛṇu nārada dēvarṣē lōkānugrahakāmyayā । <br>
yatpṛchChasi paraṃ puṇyaṃ tattē vakṣyāmi kautukāt ॥ 3॥ </p>
<p>mātā mē lōkajananī himavannagasattamāt । <br>
mēnāyāṃ brahmavādinyāṃ prādurbhūtā harapriyā ॥ 4॥ </p>
<p>mahatā tapasā''rādhya śaṅkaraṃ lōkaśaṅkaram । <br>
svamēva vallabhaṃ bhējē kalēva hi kalānidhim ॥ 5॥ </p>
<p>nagānāmadhirājastu himavān virahāturaḥ । <br>
svasutāyāḥ parikṣīṇē vasiṣṭhēna prabōdhitaḥ ॥ 6॥ </p>
<p>trilōkajananī sēyaṃ prasannā tvayi puṇyataḥ । <br>
prādurbhūtā sutātvēna tadviyōgaṃ śubhaṃ tyaja ॥ 7॥ </p>
<p>bahurūpā cha durgēyaṃ bahunāmnī sanātanī । <br>
sanātanasya jāyā sā putrīmōhaṃ tyajādhunā ॥ 8॥ </p>
<p>iti prabōdhitaḥ śailaḥ tāṃ tuṣṭāva parāṃ śivām । <br>
tadā prasannā sā durgā pitaraṃ prāha nandinī ॥ 9॥ </p>
<p>matprasādātparaṃ stōtraṃ hṛdayē pratibhāsatām । <br>
tēna nāmnāṃ sahasrēṇa pūjayan kāmamāpnuhi ॥ 10॥ </p>
<p>ityuktvāntarhitāyāṃ tu hṛdayē sphuritaṃ tadā । <br>
nāmnāṃ sahasraṃ durgāyāḥ pṛchChatē mē yaduktavān ॥ 11॥ </p>
<p>maṅgalānāṃ maṅgalaṃ tad durgānāma sahasrakam । <br>
sarvābhīṣṭapradāṃ puṃsāṃ bravīmyakhilakāmadam ॥ 12॥ </p>
<p>durgādēvī samākhyātā himavānṛṣiruchyatē । <br>
Chandōnuṣṭup japō dēvyāḥ prītayē kriyatē sadā ॥ 13॥ </p>
<p>asya śrīdurgāstōtramahāmantrasya । himavān ṛṣiḥ । anuṣṭup Chandaḥ । <br>
durgābhagavatī dēvatā । śrīdurgāprasādasiddhyarthē japē viniyōgaḥ । ।</p>
<p>śrībhagavatyai durgāyai namaḥ । </p>
<p>dēvīdhyānam <br>
ōṃ hrīṃ kālābhrābhāṃ kaṭākṣairarikulabhayadāṃ maulibaddhēndurēkhāṃ <br>
śaṅkhaṃ chakraṃ kṛpāṇaṃ triśikhamapi karairudvahantīṃ trinētrām । <br>
siṃhaskandhādhirūḍhāṃ tribhuvanamakhilaṃ tējasā pūrayantīṃ <br>
dhyāyēd durgāṃ jayākhyāṃ tridaśaparivṛtāṃ sēvitāṃ siddhikāmaiḥ ॥ </p>
<p>śrī jayadurgāyai namaḥ । </p>
<p>ōṃ śivāthōmā ramā śaktiranantā niṣkalā'malā । <br>
śāntā māhēśvarī nityā śāśvatā paramā kṣamā ॥ 1॥ </p>
<p>achintyā kēvalānantā śivātmā paramātmikā । <br>
anādiravyayā śuddhā sarvajñā sarvagā'chalā ॥ 2॥ </p>
<p>ēkānēkavibhāgasthā māyātītā sunirmalā । <br>
mahāmāhēśvarī satyā mahādēvī nirañjanā ॥ 3॥ </p>
<p>kāṣṭhā sarvāntarasthā'pi chichChaktiśchātrilālitā । <br>
sarvā sarvātmikā viśvā jyōtīrūpākṣarāmṛtā ॥ 4॥ </p>
<p>śāntā pratiṣṭhā sarvēśā nivṛttiramṛtapradā ।<br>
vyōmamūrtirvyōmasaṃsthā vyōmadhārā'chyutā'tulā ॥ 5॥</p>
<p>anādinidhanā'mōghā kāraṇātmakalākulā । <br>
ṛtuprathamajā'nābhiramṛtātmasamāśrayā ॥ 6॥ </p>
<p>prāṇēśvarapriyā namyā mahāmahiṣaghātinī । <br>
prāṇēśvarī prāṇarūpā pradhānapuruṣēśvarī ॥ 7॥ </p>
<p>sarvaśaktikalā'kāmā mahiṣēṣṭavināśinī । <br>
sarvakāryaniyantrī cha sarvabhūtēśvarēśvarī ॥ 8॥ </p>
<p>aṅgadādidharā chaiva tathā mukuṭadhāriṇī । <br>
sanātanī mahānandā''kāśayōnistathēchyatē ॥ 9॥ </p>
<p>chitprakāśasvarūpā cha mahāyōgēśvarēśvarī । <br>
mahāmāyā saduṣpārā mūlaprakṛtirīśikā ॥ 10॥ </p>
<p>saṃsārayōniḥ sakalā sarvaśaktisamudbhavā । <br>
saṃsārapārā durvārā durnirīkṣā durāsadā ॥ 11॥ </p>
<p>prāṇaśaktiścha sēvyā cha yōginī paramākalā । <br>
mahāvibhūtirdurdarśā mūlaprakṛtisambhavā ॥ 12॥ </p>
<p>anādyanantavibhavā parārthā puruṣāraṇiḥ । <br>
sargasthityantakṛchchaiva sudurvāchyā duratyayā ॥ 13॥ </p>
<p>śabdagamyā śabdamāyā śabdākhyānandavigrahā । <br>
pradhānapuruṣātītā pradhānapuruṣātmikā ॥ 14॥ </p>
<p>purāṇī chinmayā puṃsāmiṣṭadā puṣṭirūpiṇī । <br>
pūtāntarasthā kūṭasthā mahāpuruṣasañjñitā ॥ 15॥ </p>
<p>janmamṛtyujarātītā sarvaśaktisvarūpiṇī । <br>
vāñChāpradā'navachChinnapradhānānupravēśinī ॥ 16॥ </p>
<p>kṣētrajñā'chintyaśaktistu prōchyatē'vyaktalakṣaṇā । <br>
malāpavarjitā''nādimāyā tritayatattvikā ॥ 17॥ </p>
<p>prītiścha prakṛtiśchaiva guhāvāsā tathōchyatē । <br>
mahāmāyā nagōtpannā tāmasī cha dhruvā tathā ॥ 18॥ </p>
<p>vyaktā'vyaktātmikā kṛṣṇā raktā śuklā hyakāraṇā । <br>
prōchyatē kāryajananī nityaprasavadharmiṇī ॥ 19॥ </p>
<p>sargapralayamuktā cha sṛṣṭisthityantadharmiṇī । <br>
brahmagarbhā chaturviṃśasvarūpā padmavāsinī ॥ 20॥ </p>
<p>achyutāhlādikā vidyudbrahmayōnirmahālayā । <br>
mahālakṣmī samudbhāvabhāvitātmāmahēśvarī ॥ 21॥ </p>
<p>mahāvimānamadhyasthā mahānidrā sakautukā । <br>
sarvārthadhāriṇī sūkṣmā hyaviddhā paramārthadā ॥ 22॥ </p>
<p>anantarūpā'nantārthā tathā puruṣamōhinī । <br>
anēkānēkahastā cha kālatrayavivarjitā ॥ 23॥ </p>
<p>brahmajanmā haraprītā matirbrahmaśivātmikā । <br>
brahmēśaviṣṇusampūjyā brahmākhyā brahmasañjñitā ॥ 24॥ </p>
<p>vyaktā prathamajā brāhmī mahārātrīḥ prakīrtitā । <br>
jñānasvarūpā vairāgyarūpā hyaiśvaryarūpiṇī ॥ 25॥ </p>
<p>dharmātmikā brahmamūrtiḥ pratiśrutapumarthikā । <br>
apāṃyōniḥ svayambhūtā mānasī tattvasambhavā ॥ 26॥ </p>
<p>īśvarasya priyā prōktā śaṅkarārdhaśarīriṇī । <br>
bhavānī chaiva rudrāṇī mahālakṣmīstathā'mbikā ॥ 27॥ </p>
<p>mahēśvarasamutpannā bhuktimukti pradāyinī । <br>
sarvēśvarī sarvavandyā nityamuktā sumānasā ॥ 28॥ </p>
<p>mahēndrōpēndranamitā śāṅkarīśānuvartinī । <br>
īśvarārdhāsanagatā māhēśvarapativratā ॥ 29॥ </p>
<p>saṃsāraśōṣiṇī chaiva pārvatī himavatsutā । <br>
paramānandadātrī cha guṇāgryā yōgadā tathā ॥ 30॥ </p>
<p>jñānamūrtiścha sāvitrī lakṣmīḥ śrīḥ kamalā tathā । <br>
anantaguṇagambhīrā hyurōnīlamaṇiprabhā ॥ 31॥ </p>
<p>sarōjanilayā gaṅgā yōgidhyēyā'surārdinī । <br>
sarasvatī sarvavidyā jagajjyēṣṭhā sumaṅgalā ॥ 32॥ </p>
<p>vāgdēvī varadā varyā kīrtiḥ sarvārthasādhikā । <br>
vāgīśvarī brahmavidyā mahāvidyā suśōbhanā ॥ 33॥ </p>
<p>grāhyavidyā vēdavidyā dharmavidyā''tmabhāvitā । <br>
svāhā viśvambharā siddhiḥ sādhyā mēdhā dhṛtiḥ kṛtiḥ ॥ 34॥ </p>
<p>sunītiḥ saṅkṛtiśchaiva kīrtitā naravāhinī । <br>
pūjāvibhāvinī saumyā bhōgyabhāg bhōgadāyinī ॥ 35॥ </p>
<p>śōbhāvatī śāṅkarī cha lōlā mālāvibhūṣitā । <br>
paramēṣṭhipriyā chaiva trilōkīsundarī mātā ॥ 36॥ </p>
<p>nandā sandhyā kāmadhātrī mahādēvī susāttvikā । <br>
mahāmahiṣadarpaghnī padmamālā'ghahāriṇī ॥ 37॥ </p>
<p>vichitramukuṭā rāmā kāmadātā prakīrtitā । <br>
pitāmbaradharā divyavibhūṣaṇa vibhūṣitā ॥ 38॥ </p>
<p>divyākhyā sōmavadanā jagatsaṃsṛṣṭivarjitā । <br>
niryantrā yantravāhasthā nandinī rudrakālikā ॥ 39॥ </p>
<p>ādityavarṇā kaumārī mayūravaravāhinī । <br>
padmāsanagatā gaurī mahākālī surārchitā ॥ 40॥ </p>
<p>aditirniyatā raudrī padmagarbhā vivāhanā । <br>
virūpākṣā kēśivāhā guhāpuranivāsinī ॥ 41॥ </p>
<p>mahāphalā'navadyāṅgī kāmarūpā saridvarā । <br>
bhāsvadrūpā muktidātrī praṇataklēśabhañjanā ॥ 42॥ </p>
<p>kauśikī gōminī rātristridaśārivināśinī । <br>
bahurūpā surūpā cha virūpā rūpavarjitā ॥ 43॥ </p>
<p>bhaktārtiśamanā bhavyā bhavabhāvavināśinī । <br>
sarvajñānaparītāṅgī sarvāsuravimardikā ॥ 44॥ </p>
<p>pikasvanī sāmagītā bhavāṅkanilayā priyā । <br>
dīkṣā vidyādharī dīptā mahēndrāhitapātinī ॥ 45॥ </p>
<p>sarvadēvamayā dakṣā samudrāntaravāsinī । <br>
akalaṅkā nirādhārā nityasiddhā nirāmayā ॥ 46॥ </p>
<p>kāmadhēnubṛhadgarbhā dhīmatī maunanāśinī । <br>
niḥsaṅkalpā nirātaṅkā vinayā vinayapradā ॥ 47॥ </p>
<p>jvālāmālā sahasrāḍhyā dēvadēvī manōmayā । <br>
subhagā suviśuddhā cha vasudēvasamudbhavā ॥ 48॥ </p>
<p>mahēndrōpēndrabhaginī bhaktigamyā parāvarā । <br>
jñānajñēyā parātītā vēdāntaviṣayā matiḥ ॥ 49॥ </p>
<p>dakṣiṇā dāhikā dahyā sarvabhūtahṛdisthitā । <br>
yōgamāyā vibhāgajñā mahāmōhā garīyasī ॥ 50॥ </p>
<p>sandhyā sarvasamudbhūtā brahmavṛkṣāśriyāditiḥ । <br>
bījāṅkurasamudbhūtā mahāśaktirmahāmatiḥ ॥ 51॥ </p>
<p>khyātiḥ prajñāvatī sañjñā mahābhōgīndraśāyinī । <br>
hīṅkṛtiḥ śaṅkarī śāntirgandharvagaṇasēvitā ॥ 52॥ </p>
<p>vaiśvānarī mahāśūlā dēvasēnā bhavapriyā । <br>
mahārātrī parānandā śachī duḥsvapnanāśinī ॥ 53॥ </p>
<p>īḍyā jayā jagaddhātrī durvijñēyā surūpiṇī । <br>
guhāmbikā gaṇōtpannā mahāpīṭhā marutsutā ॥ 54॥ </p>
<p>havyavāhā bhavānandā jagadyōniḥ prakīrtitā । <br>
jaganmātā jaganmṛtyurjarātītā cha buddhidā ॥ 55॥ </p>
<p>siddhidātrī ratnagarbhā ratnagarbhāśrayā parā । <br>
