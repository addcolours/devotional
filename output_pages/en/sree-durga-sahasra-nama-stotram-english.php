<p>surēśvarī bhūtidātrī bhūtibhūṣā prakīrtitā । <br>
pañchabrahmasamutpannā paramārthā'rthavigrahā ॥ 74॥ </p>
<p>varṇōdayā bhānumūrtirvāgvijñēyā manōjavā । <br>
