<p>sumālinī surūpā cha bhāsvinī dhāriṇī tathā । <br>
unmūlinī sarvasabhā sarvapratyayasākṣiṇī ॥ 60॥ </p>
<p>susaumyā chandravadanā tāṇḍavāsaktamānasā । <br>
sattvaśuddhikarī śuddhā malatrayavināśinī ॥ 61॥ </p>
<p>jagatttrayī jaganmūrtistrimūrtiramṛtāśrayā । <br>
vimānasthā viśōkā cha śōkanāśinyanāhatā ॥ 62॥ </p>
<p>hēmakuṇḍalinī kālī padmavāsā sanātanī । <br>
sadākīrtiḥ sarvabhūtaśayā dēvī satāmpriyā ॥ 63॥ </p>
<p>brahmamūrtikalā chaiva kṛttikā kañjamālinī । <br>
vyōmakēśā kriyāśaktirichChāśaktiḥ parāgatiḥ ॥ 64॥ </p>
<p>kṣōbhikā khaṇḍikābhēdyā bhēdābhēdavivarjitā । <br>
abhinnā bhinnasaṃsthānā vaśinī vaṃśadhāriṇī ॥ 65॥ </p>
<p>guhyaśaktirguhyatattvā sarvadā sarvatōmukhī । <br>
bhaginī cha nirādhārā nirāhārā prakīrtitā ॥ 66॥ </p>
<p>niraṅkuśapadōdbhūtā chakrahastā viśōdhikā । <br>
sragviṇī padmasambhēdakāriṇī parikīrtitā ॥ 67॥ </p>
<p>parāvaravidhānajñā mahāpuruṣapūrvajā । <br>
parāvarajñā vidyā cha vidyujjihvā jitāśrayā ॥ 68॥ </p>
<p>vidyāmayī sahasrākṣī sahasravadanātmajā । <br>
sahasraraśmiḥsatvasthā mahēśvarapadāśrayā ॥ 69॥ </p>
<p>jvālinī sanmayā vyāptā chinmayā padmabhēdikā । <br>
mahāśrayā mahāmantrā mahādēvamanōramā ॥ 70॥ </p>
<p>vyōmalakṣmīḥ siṃharathā chēkitānā'mitaprabhā । <br>
viśvēśvarī bhagavatī sakalā kālahāriṇī ॥ 71॥ </p>
<p>sarvavēdyā sarvabhadrā guhyā dūḍhā guhāraṇī । <br>
