<p>vēdaśaktirvēdamātā vēdavidyāprakāśinī । <br>
yōgēśvarēśvarī māyā mahāśaktirmahāmayī ॥ 76॥ </p>
<p>viśvāntaḥsthā viyanmūrtirbhārgavī surasundarī । <br>
surabhirnandinī vidyā nandagōpatanūdbhavā ॥ 77॥ </p>
<p>bhāratī paramānandā parāvaravibhēdikā । <br>
sarvapraharaṇōpētā kāmyā kāmēśvarēśvarī ॥ 78॥ </p>
<p>anantānandavibhavā hṛllēkhā kanakaprabhā । <br>
kūṣmāṇḍā dhanaratnāḍhyā sugandhā gandhadāyinī ॥ 79॥ </p>
<p>trivikramapadōdbhūtā chaturāsyā śivōdayā । <br>
sudurlabhā dhanādhyakṣā dhanyā piṅgalalōchanā ॥ 80॥ </p>
<p>śāntā prabhāsvarūpā cha paṅkajāyatalōchanā । <br>
indrākṣī hṛdayāntaḥsthā śivā mātā cha satkriyā ॥ 81॥ </p>
<p>girijā cha sugūḍhā cha nityapuṣṭā nirantarā । <br>
durgā kātyāyanī chaṇḍī chandrikā kāntavigrahā ॥ 82॥ </p>
<p>hiraṇyavarṇā jagatī jagadyantrapravartikā । <br>
mandarādrinivāsā cha śāradā svarṇamālinī ॥ 83॥ </p>
<p>ratnamālā ratnagarbhā vyuṣṭirviśvapramāthinī । <br>
padmānandā padmanibhā nityapuṣṭā kṛtōdbhavā ॥ 84॥ </p>
<p>nārāyaṇī duṣṭaśikṣā sūryamātā vṛṣapriyā । <br>
mahēndrabhaginī satyā satyabhāṣā sukōmalā ॥ 85॥ </p>
<p>vāmā cha pañchatapasāṃ varadātrī prakīrtitā । <br>
vāchyavarṇēśvarī vidyā durjayā duratikramā ॥ 86॥ </p>
<p>kālarātrirmahāvēgā vīrabhadrapriyā hitā । <br>
bhadrakālī jaganmātā bhaktānāṃ bhadradāyinī ॥ 87॥ </p>
<p>karālā piṅgalākārā kāmabhēttrī mahāmanāḥ । <br>
yaśasvinī yaśōdā cha ṣaḍadhvaparivartikā ॥ 88॥ </p>
<p>śaṅkhinī padminī saṅkhyā sāṅkhyayōgapravartikā । <br>
chaitrādirvatsarārūḍhā jagatsampūraṇīndrajā ॥ 89॥ </p>
<p>śumbhaghnī khēcharārādhyā kambugrīvā balīḍitā । <br>
khagārūḍhā mahaiśvaryā supadmanilayā tathā ॥ 90॥ </p>
<p>viraktā garuḍasthā cha jagatīhṛdguhāśrayā । <br>
śumbhādimathanā bhaktahṛdgahvaranivāsinī ॥ 91॥ </p>
<p>jagatttrayāraṇī siddhasaṅkalpā kāmadā tathā । <br>
sarvavijñānadātrī chānalpakalmaṣahāriṇī ॥ 92॥ </p>
<p>sakalōpaniṣadgamyā duṣṭaduṣprēkṣyasattamā । <br>
sadvṛtā lōkasaṃvyāptā tuṣṭiḥ puṣṭiḥ kriyāvatī ॥ 93॥ </p>
<p>viśvāmarēśvarī chaiva bhuktimuktipradāyinī । <br>
śivādhṛtā lōhitākṣī sarpamālāvibhūṣaṇā ॥ 94॥ </p>
<p>nirānandā triśūlāsidhanurbāṇādidhāriṇī । <br>
aśēṣadhyēyamūrtiścha dēvatānāṃ cha dēvatā ॥ 95॥ </p>
<p>varāmbikā girēḥ putrī niśumbhavinipātinī । <br>
suvarṇā svarṇalasitā'nantavarṇā sadādhṛtā ॥ 96॥ </p>
<p>śāṅkarī śāntahṛdayā ahōrātravidhāyikā । <br>
viśvagōptrī gūḍharūpā guṇapūrṇā cha gārgyajā ॥ 97॥ </p>
<p>gaurī śākambharī satyasandhā sandhyātrayīdhṛtā । <br>
sarvapāpavinirmuktā sarvabandhavivarjitā ॥ 98॥ </p>
<p>sāṅkhyayōgasamākhyātā apramēyā munīḍitā । <br>
viśuddhasukulōdbhūtā bindunādasamādṛtā ॥ 99॥ </p>
<p>śambhuvāmāṅkagā chaiva śaśitulyanibhānanā । <br>
vanamālāvirājantī anantaśayanādṛtā ॥ 100॥ </p>
<p>naranārāyaṇōdbhūtā nārasiṃhī prakīrtitā । <br>
daityapramāthinī śaṅkhachakrapadmagadādharā ॥ 101॥ </p>
<p>saṅkarṣaṇasamutpannā ambikā sajjanāśrayā । <br>
suvṛtā sundarī chaiva dharmakāmārthadāyinī ॥ 102॥ </p>
<p>mōkṣadā bhaktinilayā purāṇapuruṣādṛtā । <br>
mahāvibhūtidā''rādhyā sarōjanilayā'samā ॥ 103॥ </p>
<p>aṣṭādaśabhujā'nādirnīlōtpaladalākṣiṇī । <br>
sarvaśaktisamārūḍhā dharmādharmavivarjitā ॥ 104॥ </p>
<p>vairāgyajñānaniratā nirālōkā nirindriyā । <br>
vichitragahanādhārā śāśvatasthānavāsinī ॥ 105॥ </p>
<p>jñānēśvarī pītachēlā vēdavēdāṅgapāragā । <br>
manasvinī manyumātā mahāmanyusamudbhavā ॥ 106॥ </p>
<p>amanyuramṛtāsvādā purandarapariṣṭutā । <br>
aśōchyā bhinnaviṣayā hiraṇyarajatapriyā ॥ 107॥ </p>
<p>hiraṇyajananī bhīmā hēmābharaṇabhūṣitā । <br>
vibhrājamānā durjñēyā jyōtiṣṭōmaphalapradā ॥ 108॥ </p>
<p>mahānidrāsamutpattiranidrā satyadēvatā । <br>
dīrghā kakudminī piṅgajaṭādhārā manōjñadhīḥ ॥ 109॥ </p>
<p>mahāśrayā ramōtpannā tamaḥpārē pratiṣṭhitā । <br>
tritattvamātā trividhā susūkṣmā padmasaṃśrayā ॥ 110॥ </p>
<p>śāntyatītakalā'tītavikārā śvētachēlikā । <br>
chitramāyā śivajñānasvarūpā daityamāthinī ॥ 111॥ </p>
<p>kāśyapī kālasarpābhavēṇikā śāstrayōnikā । <br>
trayīmūrtiḥ kriyāmūrtiśchaturvargā cha darśinī ॥ 112॥ </p>
<p>nārāyaṇī narōtpannā kaumudī kāntidhāriṇī । <br>
kauśikī lalitā līlā parāvaravibhāvinī ॥ 113॥ </p>
<p>varēṇyā'dbhutamahātmyā vaḍavā vāmalōchanā । <br>
subhadrā chētanārādhyā śāntidā śāntivardhinī ॥ 114॥</p>
<p>jayādiśaktijananī śaktichakrapravartikā । <br>
triśaktijananī janyā ṣaṭsūtraparivarṇitā ॥ 115॥ </p>
<p>sudhautakarmaṇā''rādhyā yugāntadahanātmikā । <br>
saṅkarṣiṇī jagaddhātrī kāmayōniḥ kirīṭinī ॥ 116॥ </p>
<p>aindrī trailōkyanamitā vaiṣṇavī paramēśvarī । <br>
pradyumnajananī bimbasamōṣṭhī padmalōchanā ॥ 117॥ </p>
<p>madōtkaṭā haṃsagatiḥ prachaṇḍā chaṇḍavikramā । <br>
vṛṣādhīśā parātmā cha vindhyā parvatavāsinī ॥ 118॥ </p>
<p>himavanmērunilayā kailāsapuravāsinī । <br>
chāṇūrahantrī nītijñā kāmarūpā trayītanuḥ ॥ 119॥ </p>
<p>vratasnātā dharmaśīlā siṃhāsananivāsinī । <br>
vīrabhadrādṛtā vīrā mahākālasamudbhavā ॥ 120॥ </p>
<p>vidyādharārchitā siddhasādhyārādhitapādukā । <br>
śraddhātmikā pāvanī cha mōhinī achalātmikā ॥ 121॥ </p>
<p>mahādbhutā vārijākṣī siṃhavāhanagāminī । <br>
manīṣiṇī sudhāvāṇī vīṇāvādanatatparā ॥ 122॥ </p>
<p>śvētavāhaniṣēvyā cha lasanmatirarundhatī । <br>
hiraṇyākṣī tathā chaiva mahānandapradāyinī ॥ 123॥ </p>
<p>vasuprabhā sumālyāptakandharā paṅkajānanā । <br>
parāvarā varārōhā sahasranayanārchitā ॥ 124॥ </p>
<p>śrīrūpā śrīmatī śrēṣṭhā śivanāmnī śivapriyā । <br>
śrīpradā śritakalyāṇā śrīdharārdhaśarīriṇī ॥ 125॥ </p>
<p>śrīkalā'nantadṛṣṭiścha hyakṣudrārātisūdanī । <br>
raktabījanihantrī cha daityasaṅgavimardinī ॥ 126॥ </p>
<p>siṃhārūḍhā siṃhikāsyā daityaśōṇitapāyinī । <br>
sukīrtisahitāchChinnasaṃśayā rasavēdinī ॥ 127॥ </p>
<p>guṇābhirāmā nāgārivāhanā nirjarārchitā । <br>
nityōditā svayañjyōtiḥ svarṇakāyā prakīrtitā ॥ 128॥ </p>
<p>vajradaṇḍāṅkitā chaiva tathāmṛtasañjīvinī । <br>
vajrachChannā dēvadēvī varavajrasvavigrahā ॥ 129॥ </p>
<p>māṅgalyā maṅgalātmā cha mālinī mālyadhāriṇī । <br>
gandharvī taruṇī chāndrī khaḍgāyudhadharā tathā ॥ 130॥ </p>
<p>saudāminī prajānandā tathā prōktā bhṛgūdbhavā । <br>
ēkānaṅgā cha śāstrārthakuśalā dharmachāriṇī ॥ 131॥ </p>
<p>dharmasarvasvavāhā cha dharmādharmaviniśchayā । <br>
dharmaśaktirdharmamayā dhārmikānāṃ śivapradā ॥ 132॥ </p>
<p>vidharmā viśvadharmajñā dharmārthāntaravigrahā । <br>
dharmavarṣmā dharmapūrvā dharmapāraṅgatāntarā ॥ 133॥ </p>
<p>dharmōpadēṣṭrī dharmātmā dharmagamyā dharādharā । <br>
kapālinī śākalinī kalākalitavigrahā ॥ 134॥ </p>
<p>sarvaśaktivimuktā cha karṇikāradharā'kṣarā। <br>
kaṃsaprāṇaharā chaiva yugadharmadharā tathā ॥ 135॥ </p>
<p>yugapravartikā prōktā trisandhyā dhyēyavigrahā । <br>
svargāpavargadātrī cha tathā pratyakṣadēvatā ॥ 136॥ </p>
<p>ādityā divyagandhā cha divākaranibhaprabhā । <br>
padmāsanagatā prōktā khaḍgabāṇaśarāsanā ॥ 137॥ </p>
<p>śiṣṭā viśiṣṭā śiṣṭēṣṭā śiṣṭaśrēṣṭhaprapūjitā । <br>
śatarūpā śatāvartā vitatā rāsamōdinī ॥ 138॥ </p>
<p>sūryēndunētrā pradyumnajananī suṣṭhumāyinī । <br>
sūryāntarasthitā chaiva satpratiṣṭhatavigrahā ॥ 139॥ </p>
<p>nivṛttā prōchyatē jñānapāragā parvatātmajā । <br>
kātyāyanī chaṇḍikā cha chaṇḍī haimavatī tathā ॥ 140॥ </p>
<p>dākṣāyaṇī satī chaiva bhavānī sarvamaṅgalā । <br>
dhūmralōchanahantrī cha chaṇḍamuṇḍavināśinī ॥ 141॥ </p>
<p>yōganidrā yōgabhadrā samudratanayā tathā । <br>
dēvapriyaṅkarī śuddhā bhaktabhaktipravardhinī ॥ 142॥ </p>
<p>triṇētrā chandramukuṭā pramathārchitapādukā । <br>
arjunābhīṣṭadātrī cha pāṇḍavapriyakāriṇī ॥ 143॥ </p>
<p>kumāralālanāsaktā harabāhūpadhānikā । <br>
vighnēśajananī bhaktavighnastōmaprahāriṇī ॥ 144॥ </p>
<p>susmitēndumukhī namyā jayāpriyasakhī tathā । <br>
anādinidhanā prēṣṭhā chitramālyānulēpanā ॥ 145॥ </p>
<p>kōṭichandrapratīkāśā kūṭajālapramāthinī । <br>
kṛtyāprahāriṇī chaiva māraṇōchchāṭanī tathā ॥ 146॥ </p>
<p>surāsurapravandyāṅghrirmōhaghnī jñānadāyinī । <br>
ṣaḍvairinigrahakarī vairividrāviṇī tathā ॥ 147॥ </p>
<p>bhūtasēvyā bhūtadātrī bhūtapīḍāvimardikā । <br>
nāradastutachāritrā varadēśā varapradā ॥ 148॥ </p>
<p>vāmadēvastutā chaiva kāmadā sōmaśēkharā । <br>
dikpālasēvitā bhavyā bhāminī bhāvadāyinī ॥ 149॥ </p>
<p>strīsaubhāgyapradātrī cha bhōgadā rōganāśinī । <br>
vyōmagā bhūmigā chaiva munipūjyapadāmbujā । <br>
vanadurgā cha durbōdhā mahādurgā prakīrtitā ॥ 150॥ </p>
<p>phalaśrutiḥ </p>
<p>itīdaṃ kīrtidaṃ bhadra durgānāmasahasrakam । <br>
trisandhyaṃ yaḥ paṭhēnnityaṃ tasya lakṣmīḥ sthirā bhavēt ॥ 1॥ </p>
<p>grahabhūtapiśāchādipīḍā naśyatyasaṃśayam । <br>
bālagrahādipīḍāyāḥ śāntirbhavati kīrtanāt ॥ 2॥ </p>
<p>mārikādimahārōgē paṭhatāṃ saukhyadaṃ nṛṇām । <br>
vyavahārē cha jayadaṃ śatrubādhānivārakam ॥ 3॥ </p>
<p>dampatyōḥ kalahē prāptē mithaḥ prēmābhivardhakam । <br>
āyurārōgyadaṃ puṃsāṃ sarvasampatpradāyakam ॥ 4॥ </p>
<p>vidyābhivardhakaṃ nityaṃ paṭhatāmarthasādhakam । <br>
śubhadaṃ śubhakāryēṣu paṭhatāṃ śṛṇutāmapi ॥ 5॥ </p>
