                  <span style="font-family:NotoSans; line-height:150%;">
<p>virāṭanagaraṃ ramyaṃ gachChamānō yudhiṣṭhiraḥ ।<br>
astuvanmanasā dēvīṃ durgāṃ tribhuvanēśvarīm ॥ 1 ॥</p>
<p>yaśōdāgarbhasambhūtāṃ nārāyaṇavarapriyām ।<br>
nandagōpakulējātāṃ maṅgaḻyāṃ kulavardhanīm ॥ 2 ॥</p>
<p>kaṃsavidrāvaṇakarīṃ asurāṇāṃ kṣayaṅkarīm ।<br>
śilātaṭavinikṣiptāṃ ākāśaṃ pratigāminīm ॥ 3 ॥</p>
<p>vāsudēvasya bhaginīṃ divyamālya vibhūṣitām ।<br>
divyāmbaradharāṃ dēvīṃ khaḍgakhēṭakadhāriṇīm ॥ 4 ॥</p>
<p>bhārāvataraṇē puṇyē yē smaranti sadāśivām ।<br>
tānvai tārayatē pāpāt paṅkēgāmiva durbalām ॥ 5 ॥</p>
<p>stōtuṃ prachakramē bhūyō vividhaiḥ stōtrasambhavaiḥ ।<br>
āmantrya darśanākāṅkṣī rājā dēvīṃ sahānujaḥ ॥ 6 ॥</p>
<p>namō'stu varadē kṛṣṇē kumāri brahmachāriṇi ।<br>
bālārka sadṛśākārē pūrṇachandranibhānanē ॥ 7 ॥</p>
<p>chaturbhujē chaturvaktrē pīnaśrōṇipayōdharē ।<br>
mayūrapiñChavalayē kēyūrāṅgadadhāriṇi ॥ 8 ॥</p>
<p>bhāsi dēvi yadā padmā nārāyaṇaparigrahaḥ ।<br>
svarūpaṃ brahmacharyaṃ cha viśadaṃ tava khēchari ॥ 9 ॥</p>
<p>kṛṣṇachChavisamā kṛṣṇā saṅkarṣaṇasamānanā ।<br>
bibhratī vipulau bāhū śakradhvajasamuchChrayau ॥ 10 ॥</p>
<p>pātrī cha paṅkajī kaṇṭhī strī viśuddhā cha yā bhuvi ।<br>
pāśaṃ dhanurmahāchakraṃ vividhānyāyudhāni cha ॥ 11 ॥</p>
<p>kuṇḍalābhyāṃ supūrṇābhyāṃ karṇābhyāṃ cha vibhūṣitā ।<br>
chandravispārdhinā dēvi mukhēna tvaṃ virājasē ॥ 12 ॥</p>
<p>mukuṭēna vichitrēṇa kēśabandhēna śōbhinā ।<br>
bhujaṅgā'bhōgavāsēna śrōṇisūtrēṇa rājatā ॥ 13 ॥</p>
<p>bhrājasē chāvabaddhēna bhōgēnēvēha mandaraḥ ।<br>
dhvajēna śikhipiñChānāṃ uchChritēna virājasē ॥ 14 ॥</p>
<p>kaumāraṃ vratamāsthāya tridivaṃ pāvitaṃ tvayā ।<br>
tēna tvaṃ stūyasē dēvi tridaśaiḥ pūjyasē'pi cha ॥ 15 ॥</p>
<p>trailōkya rakṣaṇārthāya mahiṣāsuranāśini ।<br>
prasannā mē suraśrēṣṭhē dayāṃ kuru śivā bhava ॥ 16 ॥</p>
<p>jayā tvaṃ vijayā chaiva saṅgrāmē cha jayapradā ।<br>
mamā'pi vijayaṃ dēhi varadā tvaṃ cha sāmpratam ॥ 17 ॥</p>
<p>vindhyē chaiva nagaśrēṣṭē tava sthānaṃ hi śāśvatam ।<br>
kāḻi kāḻi mahākāḻi sīdhumāṃsa paśupriyē ॥ 18 ॥</p>
<p>kṛtānuyātrā bhūtaistvaṃ varadā kāmachāriṇi ।<br>
bhārāvatārē yē cha tvāṃ saṃsmariṣyanti mānavāḥ ॥ 19 ॥</p>
<p>praṇamanti cha yē tvāṃ hi prabhātē tu narā bhuvi ।<br>
na tēṣāṃ durlabhaṃ kiñchit putratō dhanatō'pi vā ॥ 20 ॥ </p>
<p>durgāttārayasē durgē tatvaṃ durgā smṛtā janaiḥ ।<br>
kāntārēṣvavapannānāṃ magnānāṃ cha mahārṇavē ॥ 21 ॥<br>
(dasyubhirvā niruddhānāṃ tvaṃ gatiḥ paramā nṛṇāma)</p>
<p>jalaprataraṇē chaiva kāntārēṣvaṭavīṣu cha ।<br>
yē smaranti mahādēvīṃ na cha sīdanti tē narāḥ ॥ 22 ॥</p>
<p>tvaṃ kīrtiḥ śrīrdhṛtiḥ siddhiḥ hrīrvidyā santatirmatiḥ ।<br>
sandhyā rātriḥ prabhā nidrā jyōtsnā kāntiḥ kṣamā dayā ॥ 23 ॥</p>
<p>nṛṇāṃ cha bandhanaṃ mōhaṃ putranāśaṃ dhanakṣayam ।<br>
vyādhiṃ mṛtyuṃ bhayaṃ chaiva pūjitā nāśayiṣyasi ॥ 24 ॥</p>
<p>sō'haṃ rājyātparibhraṣṭaḥ śaraṇaṃ tvāṃ prapannavān ।<br>
praṇataścha yathā mūrdhnā tava dēvi surēśvari ॥ 25 ॥</p>
<p>trāhi māṃ padmapatrākṣi satyē satyā bhavasva naḥ ।<br>
śaraṇaṃ bhava mē durgē śaraṇyē bhaktavatsalē ॥ 26 ॥</p>
<p>ēvaṃ stutā hi sā dēvī darśayāmāsa pāṇḍavam ।<br>
upagamya tu rājānamidaṃ vachanamabravīt ॥ 27 ॥</p>
<p>śṛṇu rājan mahābāhō madīyaṃ vachanaṃ prabhō ।<br>
bhaviṣyatyachirādēva saṅgrāmē vijayastava ॥ 28 ॥</p>
<p>mama prasādānnirjitya hatvā kaurava vāhinīm ।<br>
rājyaṃ niṣkaṇṭakaṃ kṛtvā bhōkṣyasē mēdinīṃ punaḥ ॥ 29 ॥</p>
<p>bhrātṛbhiḥ sahitō rājan prītiṃ prāpsyasi puṣkalām ।<br>
matprasādāchcha tē saukhyaṃ ārōgyaṃ cha bhaviṣyati ॥ 30 ॥</p>
<p>yē cha saṅkīrtayiṣyanti lōkē vigatakalmaṣāḥ ।<br>
tēṣāṃ tuṣṭā pradāsyāmi rājyamāyurvapussutam ॥ 31 ॥</p>
<p>pravāsē nagarē chāpi saṅgrāmē śatrusaṅkaṭē ।<br>
aṭavyāṃ durgakāntārē sāgarē gahanē girau ॥ 32 ॥</p>
<p>yē smariṣyanti māṃ rājan yathāhaṃ bhavatā smṛtā ।<br>
na tēṣāṃ durlabhaṃ kiñchidasmin lōkē bhaviṣyati ॥ 33 ॥</p>
<p>ya idaṃ paramastōtraṃ bhaktyā śṛṇuyādvā paṭhēta vā ।<br>
tasya sarvāṇi kāryāṇi sidhdhiṃ yāsyanti pāṇḍavāḥ ॥ 34 ॥</p>
<p>matprasādāchcha vassarvān virāṭanagarē sthitān ।<br>
na prajñāsyanti kuravaḥ narā vā tannivāsinaḥ ॥ 35 ॥</p>
<p>ityuktvā varadā dēvī yudhiṣṭhiramarindamam ।<br>
rakṣāṃ kṛtvā cha pāṇḍūnāṃ tatraivāntaradhīyata ॥ 38 ॥</p>
