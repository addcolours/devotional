                  <span style="font-family:NotoSans; line-height:150%;">
<p>raktabījavadhō nāma aṣṭamōdhyāya ॥</p>
<p><b>dhyānaṃ</b><br>
aruṇāṃ karuṇā taraṅgitākṣīṃ dhṛtapāśāṅkuśa puṣpabāṇachāpām ।<br>
aṇimādhibhirāvṛtāṃ mayūkhai rahamityēva vibhāvayē bhavānīm ॥</p>
<p>ṛṣiruvācha   ॥1॥</p>
<p>chaṇḍē cha nihatē daityē muṇḍē cha vinipātitē ।<br>
bahuḻēṣu cha sainyēṣu kṣayitēṣvasurēśvaraḥ ॥ 2 ॥</p>
<p>tataḥ kōpaparādhīnachētāḥ śumbhaḥ pratāpavān ।<br>
udyōgaṃ sarva sainyānāṃ daityānāmādidēśa ha ॥3॥           </p>
<p>adya sarva balairdaityāḥ ṣaḍaśītirudāyudhāḥ ।<br>
kambūnāṃ chaturaśītirniryāntu svabalairvṛtāḥ   ॥4॥           </p>
<p>kōṭivīryāṇi pañchāśadasurāṇāṃ kulāni vai ।<br>
śataṃ kulāni dhaumrāṇāṃ nirgachChantu mamājñayā   ॥5॥           </p>
<p>kālakā daurhṛdā maurvāḥ kāḻikēyāstathāsurāḥ ।<br>
yuddhāya sajjā niryāntu ājñayā tvaritā mama   ॥6॥           </p>
<p>ityājñāpyāsurāpatiḥ śumbhō bhairavaśāsanaḥ ।<br>
nirjagāma mahāsainyasahastrairbhahubhirvṛtaḥ   ॥7॥</p>
<p>āyāntaṃ chaṇḍikā dṛṣṭvā tatsainyamatibhīṣaṇam ।<br>
jyāsvanaiḥ pūrayāmāsa dharaṇīgaganāntaram   ॥8॥           </p>
<p>tataḥsiṃho mahānādamatīva kṛtavānnṛpa ।<br>
ghaṇṭāsvanēna tānnādānambikā chōpabṛṃhayat   ॥9॥           </p>
<p>dhanurjyāsiṃhaghaṇṭānāṃ nādāpūritadiṅmukhā ।<br>
ninādairbhīṣaṇaiḥ kāḻī jigyē vistāritānanā   ॥10॥           </p>
<p>taṃ ninādamupaśrutya daitya sainyaiśchaturdiśam ।<br>
dēvī siṃhastathā kāḻī sarōṣaiḥ parivāritāḥ॥11॥           </p>
<p>ētasminnantarē bhūpa vināśāya suradviṣām ।<br>
bhavāyāmarasiṃhanāmativīryabalānvitāḥ   ॥12॥</p>
<p>brahmēśaguhaviṣṇūnāṃ tathēndrasya cha śaktayaḥ ।<br>
śarīrēbhyōviniṣkramya tadrūpaiśchaṇḍikāṃ yayuḥ   ॥13॥</p>
<p>yasya dēvasya yadrūpaṃ yathā bhūṣaṇavāhanam ।<br>
tadvadēva hi tachchaktirasurānyōddhumāyamau     ॥14॥</p>
<p>haṃsayuktavimānāgrē sākṣasūtraka maṇḍaluḥ ।<br>
āyātā brahmaṇaḥ śaktibrahmāṇī tyabhidhīyatē     ॥15॥</p>
<p>mahēśvarī vṛṣārūḍhā triśūlavaradhāriṇī ।<br>
mahāhivalayā prāptāchandrarēkhāvibhūṣaṇā     ॥16॥</p>
<p>kaumārī śaktihastā cha mayūravaravāhanā ।<br>
yōddhumabhyāyayau daityānambikā guharūpiṇī   ॥17॥</p>
<p>tathaiva vaiṣṇavī śaktirgaruḍōpari saṃsthitā ।<br>
śaṅkhachakragadhāśāṅkhar khaḍgahastābhyupāyayau     ॥18॥</p>
<p>yajñavārāhamatulaṃ rūpaṃ yā bhibhratō harēḥ ।<br>
śaktiḥ sāpyāyayau tatra vārāhīṃ bibhratī tanum   ॥19॥</p>
<p>nārasiṃhī nṛsiṃhasya bibhratī sadṛśaṃ vapuḥ ।<br>
prāptā tatra saṭākṣēpakṣiptanakṣatra saṃhatiḥ   ॥20॥</p>
<p>vajra hastā tathaivaindrī gajarājō paristhitā ।<br>
prāptā sahasra nayanā yathā śakrastathaiva sā     ॥21॥</p>
<p>tataḥ parivṛttastābhirīśānō dēva śaktibhiḥ ।<br>
hanyantāmasurāḥ śīghraṃ mama prītyāha chaṇḍikāṃ   ॥22॥</p>
<p>tatō dēvī śarīrāttu viniṣkrāntātibhīṣaṇā ।<br>
chaṇḍikā śaktiratyugrā śivāśataninādinī   ॥23॥</p>
<p>sā chāha dhūmrajaṭilaṃ īśānamaparājitā ।<br>
dūtatvaṃ gachCha bhagavan pārśvaṃ śumbhaniśumbhayōḥ   ॥24॥</p>
<p>brūhi śumbhaṃ niśumbhaṃ cha dānavāvatigarvitau ।<br>
yē chānyē dānavāstatra yuddhāya samupasthitāḥ   ॥25॥</p>
<p>trailōkyamindrō labhatāṃ dēvāḥ santu havirbhujaḥ ।<br>
yūyaṃ prayāta pātāḻaṃ yadi jīvitumichChatha   ॥26॥</p>
<p>balāvalēpādatha chēdbhavantō yuddhakāṅkṣiṇaḥ ।<br>
tadā gachChata tṛpyantu machChivāḥ piśitēna vaḥ     ॥27॥</p>
<p>yatō niyuktō dautyēna tayā dēvyā śivaḥ svayam ।<br>
śivadūtīti lōkē'smiṃstataḥ sā khyāti māgatā     ॥28॥</p>
<p>tē'pi śrutvā vachō dēvyāḥ śarvākhyātaṃ mahāsurāḥ ।<br>
amarṣāpūritā jagmuryatra kātyāyanī sthitā     ॥29॥</p>
<p>tataḥ prathamamēvāgrē śaraśaktyṛṣṭivṛṣṭibhiḥ ।<br>
vavarṣuruddhatāmarṣāḥ stāṃ dēvīmamarārayaḥ     ॥30॥</p>
<p>sā cha tān prahitān bāṇān ñChūlaśaktiparaśvadhān ।<br>
chichChēda līlayādhmātadhanurmuktairmahēṣubhiḥ     ॥31॥</p>
<p>tasyāgratastathā kāḻī śūlapātavidāritān ।<br>
khaṭvāṅgapōthitāṃśchārīnkurvantī vyacharattadā     ॥32॥</p>
<p>kamaṇḍalujalākṣēpahatavīryān hataujasaḥ ।<br>
brahmāṇī chākarōchChatrūnyēna yēna sma dhāvati     ॥33॥</p>
<p>māhēśvarī triśūlēna tathā chakrēṇa vaiṣṇavī ।<br>
daityāṅjaghāna kaumārī tathā śatyāti kōpanā     ॥34॥</p>
<p>aindrī kuliśapātēna śataśō daityadānavāḥ ।<br>
pēturvidāritāḥ pṛthvyāṃ rudhiraughapravarṣiṇaḥ     ॥35॥</p>
<p>tuṇḍaprahāravidhvastā daṃṣṭrā grakṣata vakṣasaḥ ।<br>
vārāhamūrtyā nyapataṃśchakrēṇa cha vidāritāḥ     ॥36॥</p>
<p>nakhairvidāritāṃśchānyān bhakṣayantī mahāsurān ।<br>
nārasiṃhī chachārājau nādā pūrṇadigambarā     ॥37॥</p>
<p>chaṇḍāṭṭahāsairasurāḥ śivadūtyabhidūṣitāḥ ।<br>
pētuḥ pṛthivyāṃ patitāṃstāṃśchakhādātha sā tadā   ॥38॥</p>
<p>iti mātṛ gaṇaṃ kruddhaṃ marda yantaṃ mahāsurān ।<br>
dṛṣṭvābhyupāyairvividhairnēśurdēvārisainikāḥ     ॥39॥</p>
<p>palāyanaparāndṛṣṭvā daityānmātṛgaṇārditān ।<br>
yōddhumabhyāyayau kruddhō raktabījō mahāsuraḥ   ॥40॥</p>
<p>raktabinduryadā bhūmau patatyasya śarīrataḥ ।<br>
samutpatati mēdinyāṃ tatpramāṇō mahāsuraḥ   ॥41॥</p>
<p>yuyudhē sa gadāpāṇirindraśaktyā mahāsuraḥ ।<br>
tataśchaindrī svavajrēṇa raktabījamatāḍayat     ॥42॥</p>
<p>kuliśēnāhatasyāśu bahu susrāva śōṇitam ।<br>
samuttasthustatō yōdhāstadrapāstatparākramāḥ     ॥43॥</p>
<p>yāvantaḥ patitāstasya śarīrādraktabindavaḥ ।<br>
tāvantaḥ puruṣā jātāḥ stadvīryabalavikramāḥ     ॥44॥</p>
<p>tē chāpi yuyudhustatra puruṣā rakta sambhavāḥ ।<br>
samaṃ mātṛbhiratyugraśastrapātātibhīṣaṇaṃ   ॥45॥</p>
<p>punaścha vajra pātēna kṣata maśya śirō yadā ।<br>
vavāha raktaṃ puruṣāstatō jātāḥ sahasraśaḥ     ॥46॥</p>
<p>vaiṣṇavī samarē chainaṃ chakrēṇābhijaghāna ha ।<br>
gadayā tāḍayāmāsa aindrī tamasurēśvaram॥47॥</p>
<p>vaiṣṇavī chakrabhinnasya rudhirasrāva sambhavaiḥ ।<br>
sahasraśō jagadvyāptaṃ tatpramāṇairmahāsuraiḥ     ॥48॥</p>
<p>śaktyā jaghāna kaumārī vārāhī cha tathāsinā ।<br>
māhēśvarī triśūlēna raktabījaṃ mahāsuram     ॥49॥</p>
<p>sa chāpi gadayā daityaḥ sarvā ēvāhanat pṛthak ।<br>
mātṝḥ kōpasamāviṣṭō raktabījō mahāsuraḥ     ॥50॥</p>
<p>tasyāhatasya bahudhā śaktiśūlādi bhirbhuviḥ । <br>
papāta yō vai raktaughastēnāsañchataśō'surāḥ     ॥51॥</p>
<p>taiśchāsurāsṛksambhūtairasuraiḥ sakalaṃ jagat ।<br>
vyāptamāsīttatō dēvā bhayamājagmuruttamam   ॥52॥</p>
<p>tān viṣaṇṇā n surān dṛṣṭvā chaṇḍikā prāhasatvaram ।<br>
uvācha kāḻīṃ chāmuṇḍē vistīrṇaṃ vadanaṃ kuru     ॥53॥</p>
<p>machChastrapātasambhūtān raktabindūn mahāsurān ।<br>
raktabindōḥ pratīchCha tvaṃ vaktrēṇānēna vēginā     ॥54॥</p>
<p>bhakṣayantī chara raṇō tadutpannānmahāsurān ।<br>
ēvamēṣa kṣayaṃ daityaḥ kṣēṇa raktō gamiṣyati     ॥55॥</p>
<p>bhakṣya māṇā stvayā chōgrā na chōtpatsyanti chāparē ।<br>
ityuktvā tāṃ tatō dēvī śūlēnābhijaghāna tam   ॥56॥</p>
<p>mukhēna kāḻī jagṛhē raktabījasya śōṇitam ।<br>
tatō'sāvājaghānātha gadayā tatra chaṇḍikāṃ     ॥57॥</p>
<p>na chāsyā vēdanāṃ chakrē gadāpātō'lpikāmapi ।<br>
tasyāhatasya dēhāttu bahu susrāva śōṇitam   ॥58॥</p>
<p>yatastatastadvaktrēṇa chāmuṇḍā sampratīchChati ।<br>
mukhē samudgatā yē'syā raktapātānmahāsurāḥ   ॥59॥</p>
<p>tāṃśchakhādātha chāmuṇḍā papau tasya cha śōṇitam   ॥60॥</p>
<p>dēvī śūlēna vajrēṇa bāṇairasibhir ṛṣṭibhiḥ ।<br>
jaghāna raktabījaṃ taṃ chāmuṇḍā pīta śōṇitam   ॥61॥</p>
<p>sa papāta mahīpṛṣṭhē śastrasaṅghasamāhataḥ ।<br>
nīraktaścha mahīpāla raktabījō mahāsuraḥ   ॥62॥</p>
<p>tatastē harṣa matulaṃ avāpustridaśā nṛpa ।<br>
tēṣāṃ mātṛgaṇō jātō nanartāsṛṃṅgamadōddhataḥ   ॥63॥</p>
<p>॥ svasti śrī mārkaṇḍēya purāṇē sāvarnikē manvantarē dēvi mahatmyē raktabījavadhōnāma aṣṭamōdhyāya samāptam ॥</p>
<p><b>āhuti</b><br>
ōṃ jayantī sāṅgāyai saśaktikāyai saparivārāyai savāhanāyai raktākṣyai aṣṭamātṛ sahitāyai mahāhutiṃ samarpayāmi namaḥ svāhā ॥<br>
