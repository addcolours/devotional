<span style="font-family:NotoSans; line-height:150%;">
<p>śrī dēvyuvācha ।<br>
mama nāma sahasraṃ cha śiva pūrvavinirmitam ।<br>
tatpaṭhyatāṃ vidhānēna tathā sarvaṃ bhaviṣyati ॥</p>
<p>ityuktvā pārvatī dēvi śrāvayāmāsa tachchatān ।<br>
tadēva nāmasāhasraṃ dakārādi varānanē ॥</p>
<p>rōgadāridryadaurbhāgyaśōkaduḥkhavināśakam ।<br>
sarvāsāṃ pūjitaṃ nāma śrīdurgādēvatā matā ॥</p>
<p>nijabījaṃ bhavēdbījaṃ mantraṃ kīlakamuchyatē ।<br>
sarvāśāpūraṇē dēvī viniyōgaḥ prakīrtitaḥ ॥</p>
<p>ōṃ asya śrīdakārādi durgāsahasranāma stōtrasya śiva ṛṣiḥ anuṣṭupChandaḥ śrīdurgā dēvatā duṃ bījaṃ duṃ kīlakaṃ duḥkhadāridryarōgaśōka nivṛttyarthaṃ pāṭhē viniyōgaḥ ।</p>
<p><b>dhyānam</b><br>
vidyuddāmasamaprabhāṃ mṛgapati skandhasthitāṃ bhīṣaṇāṃ<br>
kanyābhiḥ karavālakhēṭaviladdastābhirāsēvitām ।<br>
hasaiśchakragadāsikhēṭa viśikhāṃśchāpaṃ guṇaṃ tarjanīṃ<br>
bibhrāṇāmanalātmikāṃ śaśidharāṃ durgāṃ trinētrāṃ bhajē ॥</p>
<p><b>stōtram</b><br>
duṃ durgā durgatiharā durgāchalanivāsinī ।<br>
durgamārgānusañchārā durgamārganivāsinī ॥ 1 ॥</p>
<p>durgamārgapraviṣṭā cha durgamārgapravēśinī ।<br>
durgamārgakṛtāvāsā durgamārgajayapriyā ॥ 2 ॥</p>
<p>durgamārgagṛhītārchā durgamārgasthitātmikā ।<br>
durgamārgastutiparā durgamārgasmṛtiḥ parā ॥ 3 ॥</p>
<p>durgamārgasadāsthātrī durgamārgaratipriyā ।<br>
durgamārgasthalasthānā durgamārgavilāsinī ॥ 4 ॥</p>
<p>durgamārgatyaktavastrā durgamārgapravartinī ।<br>
durgāsuranihantrī cha durgāsuraniṣūdinī ॥ 5 ॥</p>
<p>durgāsuraharā dūtī durgāsuravināśinī ।<br>
durgāsuravadhōnmattā durgāsuravadhōtsukā ॥ 6 ॥</p>
<p>durgāsuravadhōtsāhā durgāsuravadhōdyatā ।<br>
durgāsuravadhaprēpsuḥ durgāsuramukhāntakṛt ॥ 7 ॥</p>
<p>durgāsuradhvaṃsatōṣā durgadānavadāriṇī ।<br>
durgavidrāvaṇakarī durgavidrāvaṇī sadā ॥ 8 ॥</p>
<p>durgavikṣōbhaṇakarī durgaśīrṣanikṛntanī ।<br>
durgavidhvaṃsanakarī durgadaityanikṛntanī ॥ 9 ॥</p>
<p>durgadaityaprāṇaharā durgadaityāntakāriṇī ।<br>
durgadaityaharatrātā durgadaityāsṛgunmadā ॥ 10 ॥</p>
<p>durgadaityāśanakarī durgacharmāmbarāvṛtā ।<br>
durgayuddhōtsavakarī durgayuddhaviśāradā ॥ 11 ॥</p>
<p>durgayuddhāsavaratā durgayuddhavimardinī ।<br>
durgayuddhahāsyaratā durgayuddhāṭṭahāsinī ॥ 12 ॥</p>
<p>durgayuddhamahāmattā durgayuddhānusāriṇī ।<br>
durgayuddhōtsavōtsāhā durgadēśaniṣēviṇī ॥ 13 ॥</p>
<p>durgadēśavāsaratā durgadēśavilāsinī ।<br>
durgadēśārchanaratā durgadēśajanapriyā ॥ 14 ॥</p>
<p>durgamasthānasaṃsthānā durgamadhyānusādhanā ।<br>
durgamā durgamadhyānā durgamātmasvarūpiṇī ॥ 15 ॥</p>
<p>durgamāgamasandhānā durgamāgamasaṃstutā ।<br>
durgamāgamadurjñēyā durgamaśrutisammatā ॥ 16 ॥</p>
<p>durgamaśrutimānyā cha durgamaśrutipūjitā ।<br>
durgamaśrutisuprītā durgamaśrutiharṣadā ॥ 17 ॥</p>
<p>durgamaśrutisaṃsthānā durgamaśrutimānitā ।<br>
durgamāchārasantuṣṭā durgamāchāratōṣitā ॥ 18 ॥</p>
<p>durgamāchāranirvṛttā durgamāchārapūjitā ।<br>
durgamāchārakalitā durgamasthānadāyinī ॥ 19 ॥</p>
<p>durgamaprēmaniratā durgamadraviṇapradā ।<br>
durgamāmbujamadhyasthā durgamāmbujavāsinī ॥ 20 ॥</p>
<p>durganāḍīmārgagatirdurganāḍīprachāriṇī ।<br>
durganāḍīpadmaratā durganāḍyambujasthitā ॥ 21 ॥</p>
<p>durganāḍīgatāyātā durganāḍīkṛtāspadā ।<br>
durganāḍīrataratā durganāḍīśasaṃstutā ॥ 22 ॥</p>
<p>durganāḍīśvararatā durganāḍīśachumbitā ।<br>
durganāḍīśakrōḍasthā durganāḍyatthitōtsukā ॥ 23 ॥</p>
<p>durganāḍyārōhaṇā cha durganāḍīniṣēvitā ।<br>
daristhānā daristhānavāsinī danujāntakṛt ॥ 24 ॥</p>
<p>darīkṛtatapasyā cha darīkṛtaharārchanā ।<br>
darījāpitadiṣṭā cha darīkṛtaratipriyā ॥ 25 ॥</p>
<p>darīkṛtaharārhā cha darīkrīḍitaputrikā ।<br>
darīsandarśanaratā darīrōpitavṛśchikā ॥ 26 ॥</p>
<p>darīguptikautukāḍhyā darībhramaṇatatparā ।<br>
danujāntakarī dīnā danusantānadāriṇī ॥ 27 ॥</p>
<p>danujadhvaṃsinī dūnā danujēndravināśinī ।<br>
dānavadhvaṃsinī dēvī dānavānāṃ bhayaṅkarī ॥ 28 ॥</p>
<p>dānavī dānavārādhyā dānavēndravarapradā ।<br>
dānavēndranihantrī cha dānavadvēṣiṇīsatī ॥ 29 ॥</p>
<p>dānavāriprēmaratā dānavāriprapūjitā ।<br>
dānavārikṛtārchā cha dānavārivibhūtidā ॥ 30 ॥</p>
<p>dānavārimahānandā dānavāriratipriyā ।<br>
dānavāridānaratā dānavārikṛtāspadā ॥ 31 ॥</p>
<p>dānavāristutiratā dānavārismṛtipriyā ।<br>
dānavāryāhāraratā dānavāriprabōdhinī ॥ 32 ॥</p>
<p>dānavāridhṛtaprēmā duḥkhaśōkavimōchanī ।<br>
duḥkhahantrī duḥkhadātrī duḥkhanirmūlakāriṇī ॥ 33 ॥</p>
<p>duḥkhanirmūlanakarī duḥkhadāridryanāśinī । [dāryarināśinī]<br>
duḥkhaharā duḥkhanāśā duḥkhagrāmā durāsadā ॥ 34 ॥</p>
<p>duḥkhahīnā duḥkhadūrā draviṇāchāradāyinī ।<br>
draviṇōtsargasantuṣṭā draviṇatyāgatōṣitā ॥ 35 ॥</p>
<p>draviṇasparśasantuṣṭā draviṇasparśamānadā ।<br>
draviṇasparśaharṣāḍhyā draviṇasparśatuṣṭidā ॥ 36 ॥</p>
<p>draviṇasparśanakarī draviṇasparśanāturā ।<br>
draviṇasparśanōtsāhā draviṇasparśasādhitā ॥ 37 ॥</p>
<p>draviṇasparśanamatā draviṇasparśaputrikā ।<br>
draviṇasparśarakṣiṇī draviṇastōmadāyinī ॥ 38 ॥</p>
<p>draviṇākarṣaṇakarī draviṇaughavisarjanī ।<br>
draviṇāchaladānāḍhyā draviṇāchalavāsinī ॥ 39 ॥</p>
<p>dīnamātā dīnabandhurdīnavighnavināśinī ।<br>
dīnasēvyā dīnasiddhā dīnasādhyā digambarī ॥ 40 ॥</p>
<p>dīnagēhakṛtānandā dīnagēhavilāsinī ।<br>
dīnabhāvaprēmaratā dīnabhāvavinōdinī ॥ 41 ॥</p>
<p>dīnamānavachētaḥsthā dīnamānavaharṣadā ।<br>
dīnadainyavighātēchChurdīnadraviṇadāyinī ॥ 42 ॥</p>
<p>dīnasādhanasantuṣṭā dīnadarśanadāyinī ।<br>
dīnaputrādidātrī cha dīnasamyagvidhāyinī ॥ 43 ॥</p>
<p>dattātrēyadhyānaratā dattātrēyaprapūjitā ।<br>
dattātrēyarṣisaṃsiddhā dattātrēyavibhāvitā ॥ 44 ॥</p>
<p>dattātrēyakṛtārhā cha dattātrēyaprasāditā ।<br>
dattātrēyaharṣadātrī dattātrēyasukhapradā ॥ 45 ॥</p>
<p>dattātrēyastutā chaiva dattātrēyanutāsadā ।<br>
dattātrēyaprēmaratā dattātrēyānumānitā ॥ 46 ॥</p>
<p>dattātrēyasamudgītā dattātrēyakuṭumbinī ।<br>
dattātrēyaprāṇatulyā dattātrēyaśarīriṇī ॥ 47 ॥</p>
<p>dattātrēyakṛtānandā dattātrēyāṃśasambhavā ।<br>
dattātrēyavibhūtisthā dattātrēyānusāriṇī ॥ 48 ॥</p>
<p>dattātrēyagītiratā dattātrēyadhanapradā ।<br>
dattātrēyaduḥkhaharā dattātrēyavarapradā ॥ 49 ॥</p>
<p>dattātrēyajñānadātrī dattātrēyabhayāpahā ।<br>
dēvakanyā dēvamānyā dēvaduḥkhavināśinī ॥ 50 ॥</p>
<p>dēvasiddhā dēvapūjyā dēvējyā dēvavanditā ।<br>
dēvamānyā dēvadhanyā dēvavighnavināśinī ॥ 51 ॥</p>
<p>dēvaramyā dēvaratā dēvakautukatatparā ।<br>
dēvakrīḍā dēvavrīḍā dēvavairivināśinī ॥ 52 ॥</p>
<p>dēvakāmā dēvarāmā dēvadviṣavināśinī ।<br>
dēvadēvapriyā dēvī dēvadānavavanditā ॥ 53 ॥</p>
<p>dēvadēvaratānandā dēvadēvavarōtsukā ।<br>
dēvadēvaprēmaratā dēvadēvapriyaṃvadā ॥ 54 ॥</p>
<p>dēvadēvaprāṇatulyā dēvadēvanitambinī ।<br>
dēvadēvahṛtamanā dēvadēvasukhāvahā ॥ 55 ॥</p>
<p>dēvadēvakrōḍaratā dēvadēvasukhapradā ।<br>
dēvadēvamahānandā dēvadēvaprachumbitā ॥ 56 ॥</p>
<p>dēvadēvōpabhuktā cha dēvadēvānusēvitā ।<br>
dēvadēvagataprāṇā dēvadēvagatātmikā ॥ 57 ॥</p>
<p>dēvadēvaharṣadātrī dēvadēvasukhapradā ।<br>
dēvadēvamahānandā dēvadēvavilāsinī ॥ 58 ॥</p>
<p>dēvadēvadharmapatnī dēvadēvamanōgatā ।<br>
dēvadēvavadhūrdēvī dēvadēvārchanapriyā ॥ 59 ॥</p>
<p>dēvadēvāṅganilayā dēvadēvāṅgaśāyinī ।<br>
dēvadēvāṅgasukhinī dēvadēvāṅgavāsinī ॥ 60 ॥</p>
<p>dēvadēvāṅgabhūṣā cha dēvadēvāṅgabhūṣaṇā ।<br>
dēvadēvapriyakarī dēvadēvāpriyāntakṛt ॥ 61 ॥</p>
<p>dēvadēvapriyaprāṇā dēvadēvapriyātmikā ।<br>
dēvadēvārchakaprāṇā dēvadēvārchakapriyā ॥ 62 ॥</p>
<p>dēvadēvārchakōtsāhā dēvadēvārchakāśrayā ।<br>
dēvadēvārchakāvighnā dēvadēvaprasūrapi ॥ 63 ॥</p>
<p>dēvadēvasya jananī dēvadēvavidhāyinī ।<br>
dēvadēvasya ramaṇī dēvadēvahṛdāśrayā ॥ 64 ॥</p>
<p>dēvadēvēṣṭadēvī cha dēvatāpavipātinī । [tāpasapālinī]<br>
dēvatābhāvasantuṣṭā dēvatābhāvatōṣitā ॥ 65 ॥</p>
<p>dēvatābhāvavaradā dēvatābhāvasiddhidā ।<br>
dēvatābhāvasaṃsiddhā dēvatābhāvasambhavā ॥ 66 ॥</p>
<p>dēvatābhāvasukhinī dēvatābhāvavanditā ।<br>
dēvatābhāvasuprītā dēvatābhāvaharṣadā ॥ 67 ॥</p>
<p>dēvatāvighnahantrī cha dēvatādviṣanāśinī ।<br>
dēvatāpūjitapadā dēvatāprēmatōṣitā ॥ 68 ॥</p>
<p>dēvatāgāranilayā dēvatāsaukhyadāyinī ।<br>
dēvatānijabhāvā cha dēvatāhṛtamānasā ॥ 69 ॥</p>
<p>dēvatākṛtapādārchā dēvatāhṛtabhaktikā ।<br>
dēvatāgarvamadhyasthā dēvatādēvatātanuḥ ॥ 70 ॥</p>
<p>duṃ&zwj;durgāyai namō nāmnī duṃ&zwj;ṣaṇmantrasvarūpiṇī ।<br>
dūnnamōmantrarūpā cha dūnnamōmūrtikātmikā ॥ 71 ॥</p>
<p>dūradarśipriyā duṣṭā duṣṭabhūtaniṣēvitā ।<br>
dūradarśiprēmaratā dūradarśipriyaṃvadā ॥ 72 ॥</p>
<p>dūradarśisiddhidātrī dūradarśipratōṣitā ।<br>
