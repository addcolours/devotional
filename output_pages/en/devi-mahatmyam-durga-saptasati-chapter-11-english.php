                  <span style="font-family:NotoSans; line-height:150%;">
<p>nārāyaṇīstutirnāma ēkādaśō'dhyāyaḥ ॥</p>
<p><b>dhyānaṃ</b><br>
ōṃ bālārkavidyutiṃ indukirīṭāṃ tuṅgakuchāṃ nayanatrayayuktām ।<br>
smēramukhīṃ varadāṅkuśapāśabhītikarāṃ prabhajē bhuvanēśīm ॥</p>
<p>ṛṣiruvācha॥1॥</p>
<p>dēvyā hatē tatra mahāsurēndrē<br>
sēndrāḥ surā vahnipurōgamāstām।<br>
kātyāyanīṃ tuṣṭuvuriṣṭalābhā-<br>
dvikāsivaktrābja vikāsitāśāḥ  ॥ 2 ॥<br>
         <br>
dēvi prapannārtiharē prasīda<br>
prasīda mātarjagatō'bhilasya।<br>
prasīdaviśvēśvari pāhiviśvaṃ<br>
tvamīśvarī dēvi charācharasya   ॥3॥           </p>
<p>ādhāra bhūtā jagatastvamēkā<br>
mahīsvarūpēṇa yataḥ sthitāsi<br>
apāṃ svarūpa sthitayā tvayaita<br>
dāpyāyatē kṛtsnamalaṅghya vīryē   ॥4॥           </p>
<p>tvaṃ vaiṣṇavīśaktiranantavīryā<br>
viśvasya bījaṃ paramāsi māyā।<br>
sammōhitaṃ dēvisamasta mētat-<br>
ttvaṃ vai prasannā bhuvi muktihētuḥ   ॥5॥           </p>
<p>vidyāḥ samastāstava dēvi bhēdāḥ।<br>
striyaḥ samastāḥ sakalā jagatsu।<br>
tvayaikayā pūritamambayaitat<br>
kātē stutiḥ stavyaparāparōktiḥ   ॥6॥           </p>
<p>sarva bhūtā yadā dēvī bhukti muktipradāyinī।<br>
tvaṃ stutā stutayē kā vā bhavantu paramōktayaḥ   ॥7॥           </p>
<p>sarvasya buddhirūpēṇa janasya hṛdi saṃsthitē।<br>
svargāpavargadē dēvi nārāyaṇi namō'stutē   ॥8॥           </p>
<p>kalākāṣṭhādirūpēṇa pariṇāma pradāyini।<br>
viśvasyōparatau śaktē nārāyaṇi namōstutē   ॥9॥           </p>
<p>sarva maṅgaḻa māṅgaḻyē śivē sarvārtha sādhikē।<br>
śaraṇyē trayambakē gaurī nārāyaṇi namō'stutē   ॥10॥           </p>
<p>sṛṣṭisthitivināśānāṃ śaktibhūtē sanātani।<br>
guṇāśrayē guṇamayē nārāyaṇi namō'stutē          ॥11॥           </p>
<p>śaraṇāgata dīnārta paritrāṇaparāyaṇē।<br>
sarvasyārtiharē dēvi nārāyaṇi namō'stutē   ॥12॥</p>
<p>haṃsayukta vimānasthē brahmāṇī rūpadhāriṇī।<br>
kauśāmbhaḥ kṣarikē dēvi nārāyaṇi namō'stutē॥13॥</p>
<p>triśūlachandrāhidharē mahāvṛṣabhavāhini।<br>
māhēśvarī svarūpēṇa nārāyaṇi namō'stutē॥14॥</p>
<p>mayūra kukkuṭavṛtē mahāśaktidharē'naghē।<br>
kaumārīrūpasaṃsthānē nārāyaṇi namōstutē॥15॥</p>
<p>śaṅkhachakragadāśārṅgagṛhītaparamāyudhē।<br>
prasīda vaiṣṇavīrūpēnārāyaṇi namō'stutē॥16॥</p>
<p>gṛhītōgramahāchakrē daṃṣtrōddhṛtavasundharē।<br>
varāharūpiṇi śivē nārāyaṇi namōstutē॥17॥</p>
<p>nṛsiṃharūpēṇōgrēṇa hantuṃ daityān kṛtōdyamē।<br>
trailōkyatrāṇasahitē nārāyaṇi namō'stutē॥18॥</p>
<p>kirīṭini mahāvajrē sahasranayanōjjvalē।<br>
vṛtraprāṇahārē chaindri nārāyaṇi namō'stutē॥19॥</p>
<p>śivadūtīsvarūpēṇa hatadaitya mahābalē।<br>
ghōrarūpē mahārāvē nārāyaṇi namō'stutē॥20॥</p>
<p>daṃṣtrākarāḻa vadanē śirōmālāvibhūṣaṇē।<br>
chāmuṇḍē muṇḍamathanē nārāyaṇi namō'stutē॥21॥</p>
<p>lakṣmī lajjē mahāvidhyē śraddhē puṣṭi svadhē dhruvē।<br>
mahārātri mahāmāyē nārāyaṇi namō'stutē॥22॥</p>
<p>mēdhē sarasvati varē bhūti bābhravi tāmasi।<br>
niyatē tvaṃ prasīdēśē nārāyaṇi namō'stutē॥23॥</p>
<p>sarvasvarūpē sarvēśē sarvaśaktisamanvitē।<br>
bhayēbhyastrāhi nō dēvi durgē dēvi namō'stutē॥24॥</p>
<p>ētattē vadanaṃ saumyaṃ lōchanatrayabhūṣitam।<br>
pātu naḥ sarvabhūtēbhyaḥ kātyāyini namō'stutē॥25॥</p>
<p>jvālākarāḻamatyugramaśēṣāsurasūdanam।<br>
triśūlaṃ pātu nō bhītirbhadrakāli namō'stutē॥26॥</p>
<p>hinasti daityatējāṃsi svanēnāpūrya yā jagat।<br>
sā ghaṇṭā pātu nō dēvi pāpēbhyō naḥ sutāniva॥27॥</p>
<p>asurāsṛgvasāpaṅkacharchitastē karōjvalaḥ।<br>
śubhāya khaḍgō bhavatu chaṇḍikē tvāṃ natā vayam॥28॥</p>
<p>rōgānaśēṣānapahaṃsi tuṣṭā<br>
ruṣṭā tu kāmā sakalānabhīṣṭān<br>
tvāmāśritānāṃ na vipannarāṇāṃ।<br>
tvāmāśritā śrayatāṃ prayānti॥29॥</p>
<p>ētatkṛtaṃ yatkadanaṃ tvayādya<br>
darmadviṣāṃ dēvi mahāsurāṇām।<br>
rūpairanēkairbhahudhātmamūrtiṃ<br>
kṛtvāmbhikē tatprakarōti kānyā॥30॥</p>
<p>vidyāsu śāstrēṣu vivēka dīpē<br>
ṣvādyēṣu vākyēṣu cha kā tvadanyā<br>
mamatvagartē'ti mahāndhakārē<br>
vibhrāmayatyētadatīva viśvam॥31॥</p>
<p>rakṣāṃsi yatrō graviṣāścha nāgā<br>
yatrārayō dasyubalāni yatra।<br>
davānalō yatra tathābdhimadhyē<br>
tatra sthitā tvaṃ paripāsi viśvam॥32॥</p>
<p>viśvēśvari tvaṃ paripāsi viśvaṃ<br>
viśvātmikā dhārayasīti viśvam।<br>
viśvēśavandhyā bhavatī bhavanti<br>
viśvāśrayā yētvayi bhaktinamrāḥ॥33॥</p>
<p>dēvi prasīda paripālaya nō'ri<br>
bhītērnityaṃ yathāsuravadādadhunaiva sadyaḥ।<br>
pāpāni sarva jagatāṃ praśamaṃ nayāśu<br>
utpātapākajanitāṃścha mahōpasargān॥34॥</p>
<p>praṇatānāṃ prasīda tvaṃ dēvi viśvārti hāriṇi।<br>
trailōkyavāsināmīḍyē lōkānāṃ varadā bhava॥35॥</p>
<p>dēvyuvācha॥36॥</p>
<p>varadāhaṃ suragaṇā paraṃ yanmanasēchchatha।<br>
taṃ vṛṇudhvaṃ prayachChāmi jagatāmupakārakam॥37॥</p>
<p>dēvā ūchuḥ॥38॥</p>
<p>sarvabādhā praśamanaṃ trailōkyasyākhilēśvari।<br>
ēvamēva tvayākārya masmadvairi vināśanam॥39॥</p>
<p>dēvyuvācha॥40॥</p>
<p>vaivasvatē'ntarē prāptē aṣṭāviṃśatimē yugē।<br>
śumbhō niśumbhaśchaivānyāvutpatsyētē mahāsurau॥41॥</p>
<p>nandagōpagṛhē jātā yaśōdāgarbha sambhavā।<br>
tatastaunāśayiṣyāmi vindhyāchalanivāsinī॥42॥</p>
<p>punarapyatiraudrēṇa rūpēṇa pṛthivītalē।<br>
avatīrya haviṣyāmi vaiprachittāṃstu dānavān॥43॥</p>
<p>bhakṣya yantyāścha tānugrān vaiprachittān mahāsurān।<br>
raktadantā bhaviṣyanti dāḍimīkusumōpamāḥ॥44॥</p>
<p>tatō māṃ dēvatāḥ svargē martyalōkē cha mānavāḥ।<br>
stuvantō vyāhariṣyanti satataṃ raktadantikām॥45॥</p>
<p>bhūyaścha śatavārṣikyāṃ anāvṛṣṭyāmanambhasi।<br>
munibhiḥ saṃstutā bhūmau sambhaviṣyāmyayōnijā॥46॥</p>
<p>tataḥ śatēna nētrāṇāṃ nirīkṣiṣyāmyahaṃ munīn<br>
kīrtiyiṣyanti manujāḥ śatākṣīmiti māṃ tataḥ॥47॥</p>
<p>tatō' hamakhilaṃ lōkamātmadēhasamudbhavaiḥ।<br>
bhariṣyāmi surāḥ śākairāvṛṣṭēḥ prāṇa dhārakaiḥ॥48॥</p>
<p>śākambharīti vikhyātiṃ tadā yāsyāmyahaṃ bhuvi।<br>
tatraiva cha vadhiṣyāmi durgamākhyaṃ mahāsuram॥49॥</p>
<p>durgādēvīti vikhyātaṃ tanmē nāma bhaviṣyati।<br>
punaśchāhaṃ yadābhīmaṃ rūpaṃ kṛtvā himāchalē॥50॥</p>
<p>rakṣāṃsi kṣayayiṣyāmi munīnāṃ trāṇa kāraṇāt।<br>
tadā māṃ munayaḥ sarvē stōṣyantyāna mramūrtayaḥ॥51॥</p>
<p>bhīmādēvīti vikhyātaṃ tanmē nāma bhaviṣyati।<br>
yadāruṇākhyastrailokyē mahābādhāṃ kariṣyati॥52॥</p>
<p>tadāhaṃ bhrāmaraṃ rūpaṃ kṛtvāsajkhyēyaṣaṭpadam।<br>
trailōkyasya hitārthāya vadhiṣyāmi mahāsuram॥53॥</p>
<p>bhrāmarīticha māṃ lōkā stadāstōṣyanti sarvataḥ।<br>
itthaṃ yadā yadā bādhā dānavōtthā bhaviṣyati॥54॥</p>
<p>tadā tadāvatīryāhaṃ kariṣyāmyarisaṅkṣayam ॥55॥</p>
<p>॥ svasti śrī mārkaṇḍēya purāṇē sāvarnikē manvantarē dēvi mahatmyē nārāyaṇīstutirnāma ēkādaśō'dhyāyaḥ samāptam ॥</p>
<p><b>āhuti</b><br>
ōṃ klīṃ jayantī sāṅgāyai saśaktikāyai saparivārāyai savāhanāyai lakṣmībījādhiṣtāyai garuḍavāhanyai nārayaṇī dēvyai-mahāhutiṃ samarpayāmi namaḥ svāhā ॥<br>
</p></span>
